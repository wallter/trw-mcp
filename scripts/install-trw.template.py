def _parse_simple_yaml(text: str) -> dict[str, str]:
    """Parse flat YAML key-value pairs (no nesting, no lists).

    Handles quoted/unquoted values, ignores comments and blank lines.
    Skips indented lines (nested structures) and list items (- prefix).
    This is intentionally minimal — install-trw.py ships without pyyaml.
    """
    result: dict[str, str] = {}
    for line in text.splitlines():
        # Skip comments, blank lines, indented lines (nested), and list items
        if not line or line[0] in ("#", " ", "\t", "-"):
            continue
        if ":" not in line:
            continue
        key, _, value = line.partition(":")
        key = key.strip()
        value = value.strip()
        # Strip inline comments (also after a closing quote: `key: "v"  # c`)
        for quote in ('"', "'"):
            if value.startswith(quote):
                end = value.find(quote, 1)
                trailing = value[end + 1 :].lstrip() if end > 0 else None
                if trailing is not None and (not trailing or trailing.startswith("#")):
                    value = value[1:end]
                    break
        else:
            # Unquoted — strip trailing inline comment
            if " #" in value:
                value = value[: value.index(" #")].rstrip()
        if key and value:
            result[key] = value
    return result


def _read_credentials_key(credentials_path: Path) -> str:
    """Return the ``platform_api_key`` in *credentials_path*, or ``""``.

    Self-contained line scan (no pyyaml, no trw_mcp import) mirroring
    ``models/config/_credentials.py::read_key_from_file`` — the installer
    template ships standalone, so it cannot import the runtime package.
    """
    try:
        text = credentials_path.read_text(encoding="utf-8")
    except (OSError, UnicodeDecodeError):
        return ""
    for line in text.splitlines():
        stripped = line.strip()
        if stripped.startswith("#") or not stripped.startswith("platform_api_key:"):
            continue
        value = stripped.partition(":")[2].strip()
        if len(value) >= 2 and value[0] == value[-1] and value[0] in ("'", '"'):
            value = value[1:-1]
        if value:
            return value
    return ""


def _resolve_prior_api_key(target_dir: Path, flat: dict[str, str]) -> str:
    """Resolve the platform API key by SEC-005 precedence (highest wins).

    Precedence (PRD-SEC-005-FR03 + PRD-INFRA-129 FR05): ``TRW_API_KEY`` env >
    ``TRW_PLATFORM_API_KEY`` env > ``.trw/credentials.yaml`` >
    ``.trw/config.yaml`` (deprecated fallback).

    ``TRW_API_KEY`` is consulted FIRST because ``scripts/install.sh`` reads the
    key from ``credentials.yaml`` and exports it ONLY under that name; before
    PRD-INFRA-129 the installer checked ``TRW_PLATFORM_API_KEY`` alone, so a
    user with a valid, just-used key hit a raw argparse error
    (sub_x2O2h3CYyzKZWLu2#c). ``TRW_PLATFORM_API_KEY`` remains accepted for the
    trw-mcp runtime config resolver's name (NFR08 — no source removed).

    *flat* is the already-parsed config.yaml mapping so config.yaml is not
    re-read.
    """
    trw_api_key = os.environ.get("TRW_API_KEY", "").strip()
    if trw_api_key:
        return trw_api_key
    env_key = os.environ.get("TRW_PLATFORM_API_KEY", "").strip()
    if env_key:
        return env_key
    creds_key = _read_credentials_key(target_dir / ".trw" / "credentials.yaml")
    if creds_key:
        return creds_key
    return flat.get("platform_api_key", "").strip()


def _load_prior_config(target_dir: Path, ui: "UI | None" = None) -> dict[str, object]:
    """Load prior installation config from .trw/config.yaml if it exists.

    Returns a dict with keys that were previously configured, or empty dict
    for first-time installs. If ``ui`` is provided and the stored
    ``target_platforms`` contains identifiers that are neither a supported
    client nor a known legacy alias, an orange warning is emitted naming the
    unknowns and the supported values — the installer then proceeds with the
    valid entries (if any).
    """
    config_path = target_dir / ".trw" / "config.yaml"
    if not config_path.is_file():
        return {}

    prior: dict[str, object] = {}
    try:
        raw_text = config_path.read_text(encoding="utf-8")
        flat = _parse_simple_yaml(raw_text)
        if "installation_id" in flat:
            prior["project_name"] = flat["installation_id"]
        # PRD-SEC-005-FR03: resolve the credential by precedence
        # (env > credentials.yaml > config.yaml). Reading config.yaml ONLY
        # broke prior-key detection + --with-proprietary auto-derive on
        # migrated installs where the key moved to the ignored credentials.yaml.
        resolved_api_key = _resolve_prior_api_key(target_dir, flat)
        if resolved_api_key:
            prior["api_key"] = resolved_api_key
        if "platform_telemetry_enabled" in flat:
            prior["telemetry"] = flat["platform_telemetry_enabled"].lower() == "true"
        if "platform_telemetry_enabled" in flat:
            # FR07: also surface platform_telemetry_enabled under its canonical
            # config key so reinstall logic can read the prior opt-out directly.
            prior["platform_telemetry_enabled"] = flat["platform_telemetry_enabled"].lower() == "true"
        if "learning_sharing_enabled" in flat:
            prior["learning_sharing_enabled"] = flat["learning_sharing_enabled"].lower() == "true"
        if "embeddings_enabled" in flat:
            prior["embeddings"] = flat["embeddings_enabled"].lower() == "true"
        if "sqlite_vec_enabled" in flat:
            prior["sqlite_vec"] = flat["sqlite_vec_enabled"].lower() == "true"
        target_platforms: list[str] = []
        platform_urls: list[str] = []
        in_target_platforms = False
        in_platform_urls = False
        for line in raw_text.splitlines():
            stripped = line.strip()
            if stripped.startswith("platform_urls:"):
                in_platform_urls = True
                in_target_platforms = False
                continue
            if stripped.startswith("target_platforms:"):
                in_target_platforms = True
                in_platform_urls = False
                continue
            if in_platform_urls:
                if stripped.startswith("- "):
                    platform_urls.append(stripped[2:].strip().strip('"').strip("'"))
                    continue
                if stripped and not stripped.startswith("#"):
                    in_platform_urls = False
            if in_target_platforms:
                if stripped.startswith("- "):
                    target_platforms.append(stripped[2:].strip().strip('"').strip("'"))
                    continue
                if stripped and not stripped.startswith("#"):
                    in_target_platforms = False
        if platform_urls:
            prior["platform_urls"] = [url for url in platform_urls if url]
        if target_platforms:
            # Non-strict: legacy aliases are applied, unknown entries dropped.
            # A stale / renamed identifier in a user's prior config must never
            # crash the installer — they get re-prompted (interactive) or fall
            # back to detection (script mode) if nothing valid remains.
            try:
                unknowns = [
                    raw.strip()
                    for raw in target_platforms
                    if raw.strip()
                    and _LEGACY_IDE_ALIASES.get(raw.strip(), raw.strip()) != "all"
                    and _LEGACY_IDE_ALIASES.get(raw.strip(), raw.strip())
                    not in _SUPPORTED_IDES
                ]
                prior["target_platforms"] = _normalize_ide_targets(
                    target_platforms, strict=False
                )
                if unknowns and ui is not None:
                    supported = ", ".join(_SUPPORTED_IDES + ["all"])
                    ui.warn(
                        "Ignoring unknown target_platforms in .trw/config.yaml: "
                        f"{', '.join(repr(u) for u in unknowns)}. "
                        f"Supported values: {supported}"
                    )
            except Exception:
                # Defence in depth: even unexpected errors here should not
                # block the installer. Treat as "no prior platform choice".
                pass
    except (OSError, UnicodeDecodeError):
        pass
    return prior


def _detect_installed_extras(python: str) -> dict[str, bool]:
    """Detect which optional extras are already installed.

    Uses a short timeout (10s) because import checks should resolve quickly.
    Longer hangs indicate missing packages or environment issues (e.g. SDK
    attempting network calls during import on system Python without a venv).
    """
    extras: dict[str, bool] = {}
    extras["ai"] = _run_quiet([python, "-c", "import anthropic"], timeout=10)
    extras["sqlite_vec"] = _run_quiet([python, "-c", "import sqlite_vec"], timeout=10)
    return extras


# ── Validation / sanitization ────────────────────────────────────────


def sanitize_project_name(name: str) -> str:
    """Lowercase, strip non-alphanumeric, collapse hyphens, truncate to 64."""
    result = name.lower()
    result = re.sub(r"[^a-z0-9]", "-", result)
    result = re.sub(r"-{2,}", "-", result)
    return result.strip("-")[:64].rstrip("-")


def validate_api_key(key: str) -> bool:
    """Check *key* matches ``trw_`` or ``trw_dk_`` prefix, base64url body, max 128.

    The body is ``secrets.token_urlsafe(32)`` (backend auth), which is base64url
    and therefore includes ``-`` and ``_`` alongside letters and digits. An
    earlier ``[a-zA-Z0-9_]`` character class falsely rejected every device key
    (``trw_dk_...`` with a hyphen in the token) — the ``-`` is now accepted.

    Uses ``re.fullmatch`` (NOT ``re.match(... "$")``): a trailing ``$`` matches
    just before a final newline, so ``re.match`` accepted ``"trw_abc\\n"`` — a
    pasted key carrying a stray newline (Codex LOW / P2-2). ``fullmatch`` anchors
    the WHOLE string and the char class excludes whitespace, so leading/trailing/
    embedded newlines, spaces, tabs, and CRLF terminators are all rejected.
    """
    return len(key) <= 128 and bool(re.fullmatch(r"trw_(dk_)?[A-Za-z0-9_-]+", key))


