def _format_ide_list(ides: list[str]) -> str:
    """Format IDE labels into a readable list."""
    labels = [_ide_label(ide) for ide in ides]
    if len(labels) <= 1:
        return labels[0] if labels else ""
    if len(labels) == 2:
        return f"{labels[0]} and {labels[1]}"
    return f"{', '.join(labels[:-1])}, and {labels[-1]}"


def _normalize_ide_targets(ides: list[str], *, strict: bool = True) -> list[str]:
    """Validate installer IDE identifiers and expand ``all``.

    Applies ``_LEGACY_IDE_ALIASES`` so that prior configs written before a
    rename keep working transparently (e.g. ``cursor`` → ``cursor-ide``).

    In strict mode (CLI use), unknown identifiers raise ``ValueError`` with a
    "did you mean?" hint. In non-strict mode (loading prior config), unknown
    identifiers are dropped — we never want a stale config entry to crash the
    installer and block the user from upgrading.
    """
    import difflib

    normalized: list[str] = []
    dropped: list[str] = []
    for raw in ides:
        ide = raw.strip()
        if not ide:
            continue
        ide = _LEGACY_IDE_ALIASES.get(ide, ide)
        if ide == "all":
            return _SUPPORTED_IDES.copy()
        if ide in _RETIRED_IDES:
            if strict:
                raise ValueError(f"--ide {ide}: {_RETIRED_IDES[ide]}")
            # Prior config carries a retired id: drop it so the upgrade
            # proceeds; the notice prints once via _load_prior_config's caller.
            print(f"{YELLOW}[TRW]{NC} Note: {_RETIRED_IDES[ide]}", file=sys.stderr)
            continue
        if ide in _SUPPORTED_IDES:
            normalized.append(ide)
        else:
            dropped.append(raw.strip())

    if dropped:
        if strict:
            supported = ", ".join(_SUPPORTED_IDES + ["all"])
            hints: list[str] = []
            for bad in dropped:
                matches = difflib.get_close_matches(bad, _SUPPORTED_IDES, n=1, cutoff=0.5)
                if matches:
                    hints.append(f"'{bad}' (did you mean '{matches[0]}'?)")
                else:
                    hints.append(f"'{bad}'")
            raise ValueError(
                f"Unknown --ide value(s): {', '.join(hints)}. Supported values: {supported}"
            )
        # Non-strict: silently drop unknowns; caller decides whether to warn.
    return _unique(normalized)


def _parse_ide_argument(raw: str | None) -> list[str] | None:
    """Parse ``--ide`` into a normalized list of targets."""
    if raw is None:
        return None
    parts = [part.strip() for part in raw.split(",")]
    normalized = _normalize_ide_targets(parts)
    if not normalized:
        raise ValueError("--ide requires at least one client identifier")
    return normalized


def _read_single_key(tty: TextIO) -> str | None:
    """Read a single keypress from *tty* for checkbox navigation."""
    if os.name == "nt":
        try:
            import msvcrt

            getwch = getattr(msvcrt, "getwch")
            ch = str(getwch())
            if ch in ("\x00", "\xe0"):
                arrow = str(getwch())
                if arrow == "H":
                    return "UP"
                if arrow == "P":
                    return "DOWN"
                return arrow
            if ch in ("\r", "\n"):
                return "ENTER"
            if ch == " ":
                return "SPACE"
            return ch
        except (ImportError, OSError):
            return None

    try:
        import termios
        import tty as tty_mod
    except ImportError:
        return None

    try:
        fd = tty.fileno()
        old = termios.tcgetattr(fd)
    except (termios.error, OSError, ValueError):
        # /dev/tty opened but is not a real terminal (some sandboxes/CI);
        # mirror the Windows branch's resilience and let the caller fall
        # back to the numbered-selection prompt.
        return None
    try:
        tty_mod.setraw(fd)
        ch = tty.read(1)
        if ch == "\x1b":
            nxt = tty.read(1)
            if nxt == "[":
                arrow = tty.read(1)
                if arrow == "A":
                    return "UP"
                if arrow == "B":
                    return "DOWN"
                return arrow
            return ch
        if ch in ("\r", "\n"):
            return "ENTER"
        if ch == " ":
            return "SPACE"
        return ch
    finally:
        termios.tcsetattr(fd, termios.TCSADRAIN, old)


def _prompt_ide_selection_fallback(default_targets: list[str]) -> list[str] | None:
    """Fallback multi-select prompt for terminals without raw-key support."""
    default_display = ",".join(str(_SUPPORTED_IDES.index(ide) + 1) for ide in default_targets)
    print()
    print("Configure TRW for one or more clients:")
    for idx, ide in enumerate(_SUPPORTED_IDES, start=1):
        meta = _IDE_META[ide]
        marker = "[x]" if ide in default_targets else "[ ]"
        print(f"  {idx}. {marker} {meta['label']} — {meta['summary']}")
    print("  s. Skip project client setup for now")

    tty = _open_tty()
    if tty is None:
        return default_targets
    try:
        sys.stdout.write(f"\nSelection [default: {default_display}]: ")
        sys.stdout.flush()
        raw = tty.readline().strip().lower()
    finally:
        tty.close()

    if not raw:
        return default_targets
    if raw in {"s", "skip"}:
        return None

    choices: list[str] = []
    try:
        for part in raw.split(","):
            number = int(part.strip())
            if not (1 <= number <= len(_SUPPORTED_IDES)):
                raise ValueError
            choices.append(_SUPPORTED_IDES[number - 1])
    except ValueError:
        return default_targets
    return _normalize_ide_targets(choices)


