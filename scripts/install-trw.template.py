def main() -> None:
    parser = argparse.ArgumentParser(
        description="Install TRW Framework in a project directory.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Interactive mode (default in a terminal):\n"
            "  Guided setup with prompts, spinners, and feature selection.\n\n"
            "Script mode (piped input or --script):\n"
            "  No prompts. Use flags to configure.\n\n"
            "Examples:\n"
            "  python3 install-trw.py                       # Interactive\n"
            "  python3 install-trw.py --ai --telemetry      # With extras\n"
            "  python3 install-trw.py --script --no-ai      # Headless\n"
            "  python3 install-trw.py --upgrade             # Upgrade only\n"
        ),
    )
    parser.add_argument("target_dir", nargs="?", default=".", help="Project directory")
    parser.add_argument("--upgrade", action="store_true", help="Upgrade package only")
    parser.add_argument("--ai", dest="install_ai", action="store_true", default=None, help="Install AI/LLM extras")
    parser.add_argument("--no-ai", dest="install_ai", action="store_false", help="Skip AI extras")
    parser.add_argument("--sqlite-vec", dest="install_vec", action="store_true", default=None, help="Install sqlite-vec")
    parser.add_argument("--no-sqlite-vec", dest="install_vec", action="store_false", help="Skip sqlite-vec")
    parser.add_argument("--quiet", "-q", action="store_true", help="Minimal output")
    parser.add_argument("--script", action="store_true", help="Force non-interactive mode")
    parser.add_argument("--name", default="", help="Project name (installation ID)")
    parser.add_argument("--api-key", default="", help="Platform API key")
    parser.add_argument("--telemetry", dest="telemetry", action="store_true", default=None, help="Enable telemetry")
    parser.add_argument("--no-telemetry", dest="telemetry", action="store_false", help="Disable telemetry")
    parser.add_argument(
        "--ide",
        choices=["claude-code", "opencode", "all"],
        default=None,
        help="Target IDE to configure (prompted if not specified in interactive mode)",
    )

    args = parser.parse_args()

    # Mode detection
    interactive = (not args.script) and sys.stdin.isatty()

    # Script-mode defaults
    install_ai = args.install_ai
    install_vec = args.install_vec
    if not interactive:
        install_ai = install_ai if install_ai is not None else False
        install_vec = install_vec if install_vec is not None else False

    # Resolve target
    target_dir = Path(args.target_dir).resolve()
    if not target_dir.is_dir():
        print(f"{RED}[TRW]{NC} Directory not found: {target_dir}", file=sys.stderr)
        sys.exit(1)

    ui = UI(interactive=interactive, quiet=args.quiet)

    show_banner(ui)

    # Load prior installation state (before python check — no python needed yet)
    prior_config = _load_prior_config(target_dir)
    is_reinstall = bool(prior_config)

    if is_reinstall and interactive:
        ui.info("Existing TRW installation detected — reusing prior settings")

    # Calculate step count
    total = 4  # python + extract + install + project-setup
    has_config = interactive or args.name or args.api_key
    # We may not know ai/vec yet in interactive mode, so add after prompt
    # Use a mutable list so phases can reference the final count
    extra = 0
    if install_ai:
        extra += 1
    if install_vec:
        extra += 1
    if has_config:
        extra += 1
    total += extra

    # Create temp dir for wheel extraction
    tmpdir = Path(tempfile.mkdtemp(prefix="trw-install-"))
    try:
        # Phase 1: Python check
        python = phase_check_python(ui, 1, total)

        # Detect already-installed extras now that we have the python path
        prior_extras: dict[str, bool] = _detect_installed_extras(python) if is_reinstall else {}

        # Phase 2: Extract wheels
        memory_whl, mcp_whl = phase_extract_wheels(ui, 2, total, tmpdir)

        # Feature prompts (between extract and install)
        if interactive:
            install_ai, install_vec = phase_prompt_features(
                ui, install_ai, install_vec, prior_extras=prior_extras,
            )
            # Recalculate total now that we know selections
            total = 4
            if install_ai:
                total += 1
            if install_vec:
                total += 1
            if has_config:
                total += 1

        install_ai = bool(install_ai)
        install_vec = bool(install_vec)

        # Phase 3: Install base packages
        phase_install_packages(ui, 3, total, python, memory_whl, mcp_whl)

        # Phase 3+: Install extras
        next_step, features = phase_install_extras(ui, 4, total, python, install_ai, install_vec)

        # Phase N: Project setup
        phase_project_setup(
            ui, next_step, total, python, target_dir, args.upgrade,
            interactive=interactive,
            ide=args.ide,
        )
        next_step += 1

        # Phase N+1: Configure
        platform_status = "offline"
        if has_config:
            platform_status = phase_configure(
                ui, next_step, total, target_dir, interactive,
                args.name, args.api_key, args.telemetry,
                prior_config=prior_config,
            )

        # Done!
        show_success_banner(ui, platform_status, features)

    finally:
        try:
            shutil.rmtree(tmpdir)
        except OSError:
            pass


if __name__ == "__main__":
    main()

