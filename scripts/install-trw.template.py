def _open_tty() -> TextIO | None:
    """Open a TTY for reading, or None if unavailable.

    Uses /dev/tty on Unix and CON on Windows.
    """
    for tty_path in ("/dev/tty", "CON"):
        try:
            return open(tty_path, encoding="utf-8")
        except OSError:
            continue
    return None


def prompt_yes_no(question: str, default: str = "n") -> bool:
    """Prompt for yes/no. Returns True for yes."""
    hint = "[Y/n]" if default == "y" else "[y/N]"
    tty = _open_tty()
    if tty is None:
        return default == "y"
    try:
        sys.stdout.write(f"    {question} {hint} ")
        sys.stdout.flush()
        answer = tty.readline().strip() or default
    finally:
        tty.close()
    return answer.lower().startswith("y")


def prompt_input(prompt_text: str, default: str = "") -> str:
    """Prompt for text input."""
    display = f"    {prompt_text}"
    if default:
        display += f" [{default}]"
    display += " "
    tty = _open_tty()
    if tty is None:
        return default
    try:
        sys.stdout.write(display)
        sys.stdout.flush()
        answer = tty.readline().strip() or default
    finally:
        tty.close()
    return answer


def _unique(values: list[str]) -> list[str]:
    """Return *values* with duplicates removed while preserving order."""
    return list(dict.fromkeys(values))


def _ide_label(ide: str) -> str:
    """Return a user-facing label for an installer IDE identifier."""
