# ── Wheel extraction ─────────────────────────────────────────────────

def _extract_wheel_data(marker: str) -> bytes:
    """Read base64 data following *marker* from this script file.

    Wheels are embedded as comment lines after marker comments at the
    end of the file (e.g. MEMORY_WHEEL_DATA, WHEEL_DATA). Each marker
    is followed by comment-prefixed base64 lines.
    """
    script_path = (
        Path(sys.argv[0]).resolve()
        if sys.argv[0] != "-"
        else Path(__file__).resolve()
    )
    text = script_path.read_text(encoding="utf-8", errors="replace")

    collecting = False
    b64_chunks: list[str] = []

    for line in text.splitlines():
        stripped = line.strip()
        if stripped == f"# {marker}":
            collecting = True
            continue
        if collecting:
            if stripped.startswith("# __") and stripped.endswith("__"):
                break
            if stripped.startswith("# "):
                b64_chunks.append(stripped[2:])
            elif stripped == "#":
                continue
            else:
                break

    if not b64_chunks:
        raise RuntimeError(f"No embedded data found for marker {marker}")

    return base64.b64decode("".join(b64_chunks))


def extract_wheels(tmpdir: Path) -> tuple[Path, Path]:
    """Extract both embedded wheels into *tmpdir*.

    Returns (memory_wheel_path, mcp_wheel_path).
    """
    memory_path = tmpdir / MEMORY_WHEEL_FILENAME
    memory_path.write_bytes(_extract_wheel_data("__MEMORY_WHEEL_DATA__"))

    mcp_path = tmpdir / WHEEL_FILENAME
    mcp_path.write_bytes(_extract_wheel_data("__WHEEL_DATA__"))

    return memory_path, mcp_path


# ── pip install with fallback ────────────────────────────────────────

def _run_quiet(cmd: list[str]) -> bool:
    """Run a command silently, return True if exit code == 0."""
    try:
        return subprocess.run(
            cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL,
        ).returncode == 0
    except FileNotFoundError:
        return False


def pip_install(python: str, package: str, label: str, ui: UI) -> bool:
    """Try pip install with escalating fallbacks.

    Tries: normal -> --user -> --break-system-packages.
    """
    base = [python, "-m", "pip", "install", "--upgrade", "--quiet"]

    if _run_quiet(base + [package]):
        return True

    if _run_quiet(base + ["--user", package]):
        ui.step_warn(f"Installed {label} with --user (PEP 668 managed environment)")
        return True

    if _run_quiet(base + ["--break-system-packages", package]):
        ui.step_warn(f"Installed {label} with --break-system-packages")
        return True

    return False


# ── Run command with live progress ───────────────────────────────────

def run_with_progress(ui: UI, fallback_msg: str, cmd: list[str]) -> bool:
    """Run *cmd* showing live progress. Returns True on success."""
    try:
        proc = subprocess.Popen(
            cmd, stdout=subprocess.PIPE, stderr=subprocess.STDOUT, text=True,
        )
    except FileNotFoundError:
        return False

    assert proc.stdout is not None

    if ui.interactive:
        ui.start_spinner(fallback_msg)
        file_count = 0
        for line in proc.stdout:
            line = line.strip()
            if re.search(r"(Updated|Created|Preserved|synced|WARNING):?", line):
                file_count += 1
                short = re.sub(r"^(Updated: |Created \(new\): |Preserved: )", "", line)[:60]
                ui.update_spinner(f"{fallback_msg} ({file_count} files) {DIM}{short}{NC}")
    else:
        for line in proc.stdout:
            if not ui.quiet:
                print(f"{GREEN}[TRW]{NC}   {line.rstrip()}")

    proc.wait()
    return proc.returncode == 0


# ── Config file update ───────────────────────────────────────────────

def update_config(
    config_path: Path,
    project_name: str,
    api_key: str,
    telemetry_enabled: bool,
) -> bool:
    """Update .trw/config.yaml with installation settings."""
    if not config_path.is_file():
        return False

    lines = config_path.read_text(encoding="utf-8").splitlines(keepends=True)
    platform_url = "https://api.trwframework.com"

    updated: set[str] = set()
    out: list[str] = []

    for line in lines:
        s = line.lstrip()

        if s.startswith("installation_id:"):
            out.append(f"installation_id: {project_name}\n")
            updated.add("installation_id")
            continue
        if s.startswith("platform_api_key:"):
            out.append(f'platform_api_key: "{api_key}"\n' if api_key else line)
            updated.add("platform_api_key")
            continue
        if s.startswith("platform_telemetry_enabled:"):
            val = "true" if telemetry_enabled else "false"
            out.append(f"platform_telemetry_enabled: {val}\n")
            updated.add("platform_telemetry_enabled")
            continue
        if s.startswith("platform_urls:"):
            updated.add("platform_urls")
            continue
        if s.startswith("- ") and "platform_urls" in updated and "platform_urls_done" not in updated:
            continue
        if "platform_urls" in updated and "platform_urls_done" not in updated and not s.startswith("- "):
            updated.add("platform_urls_done")

        # Skip commented-out platform config hints
        skip_prefixes = (
            "# platform_urls:", '#   - "https://api.',
            "# platform_api_key:", "# platform_telemetry_enabled:",
            "# Platform telemetry",
        )
        if any(s.startswith(p) for p in skip_prefixes):
            continue

        out.append(line)

    # Append missing keys
    if "installation_id" not in updated:
        out.append(f"installation_id: {project_name}\n")
    if api_key and "platform_api_key" not in updated:
        out.append(f'platform_api_key: "{api_key}"\n')
    if telemetry_enabled and "platform_telemetry_enabled" not in updated:
        out.append("platform_telemetry_enabled: true\n")
    if api_key or telemetry_enabled:
        out.append("platform_urls:\n")
        out.append(f'  - "{platform_url}"\n')

    config_path.write_text("".join(out), encoding="utf-8")
    return True


