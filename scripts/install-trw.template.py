def main() -> None:
    parser = argparse.ArgumentParser(
        description="Install TRW Framework in a project directory.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Interactive mode (default in a terminal):\n"
            "  Guided setup with prompts, spinners, and feature selection.\n\n"
            "Script mode (piped input or --script):\n"
            "  No prompts. Use flags to configure.\n\n"
            "Examples:\n"
            "  python3 install-trw.py                       # Interactive\n"
            "  python3 install-trw.py --ai --telemetry      # With extras\n"
            "  python3 install-trw.py --script --no-ai      # Headless\n"
            "  python3 install-trw.py --upgrade             # Upgrade only\n"
        ),
    )
    parser.add_argument("target_dir", nargs="?", default=".", help="Project directory")
    parser.add_argument("--upgrade", action="store_true", help="Upgrade package only")
    parser.add_argument("--ai", dest="install_ai", action="store_true", default=None, help="Install AI/LLM extras")
    parser.add_argument("--no-ai", dest="install_ai", action="store_false", help="Skip AI extras")
    parser.add_argument(
        "--sqlite-vec", dest="install_vec", action="store_true", default=None, help="Install sqlite-vec"
    )
    parser.add_argument("--no-sqlite-vec", dest="install_vec", action="store_false", help="Skip sqlite-vec")
    parser.add_argument("--quiet", "-q", action="store_true", help="Minimal output")
    parser.add_argument("--script", action="store_true", help="Force non-interactive mode")
    parser.add_argument("--name", default="", help="Project name (installation ID)")
    parser.add_argument("--api-key", default="", help="Platform API key")
    parser.add_argument("--telemetry", dest="telemetry", action="store_true", default=None, help="Enable telemetry")
    parser.add_argument("--no-telemetry", dest="telemetry", action="store_false", help="Disable telemetry")
    parser.add_argument("--skip-auth", action="store_true", help="Skip device auth flow during install")
    parser.add_argument(
        "--pip-target",
        default="",
        metavar="DIR",
        help="Install packages to DIR instead of site-packages (for tmpfs mounts)",
    )
    parser.add_argument(
        "--ide",
        choices=["claude-code", "opencode", "codex", "all"],
        default=None,
        help="Target IDE to configure (prompted if not specified in interactive mode)",
    )

    args = parser.parse_args()

    # Mode detection
    interactive = (not args.script) and sys.stdin.isatty()

    # Script-mode defaults
    install_ai = args.install_ai
    install_vec = args.install_vec
    if not interactive:
        install_ai = install_ai if install_ai is not None else False
        install_vec = install_vec if install_vec is not None else False

    # Resolve target
    target_dir = Path(args.target_dir).resolve()
    if not target_dir.is_dir():
        print(f"{RED}[TRW]{NC} Directory not found: {target_dir}", file=sys.stderr)
        sys.exit(1)

    ui = UI(interactive=interactive, quiet=args.quiet)

    show_banner(ui)

    # ── Preflight ────────────────────────────────────────────────────
    # Gather all information BEFORE showing numbered steps so the step
    # count never changes mid-flow.

    prior_config = _load_prior_config(target_dir)
    is_reinstall = bool(prior_config)

    if is_reinstall and interactive:
        ui.info("Existing TRW installation detected \u2014 reusing prior settings")

    if interactive:
        draw_divider("Preflight")

    # Python check (preflight — not a numbered step)
    python = check_python_version(ui)
    major, minor = sys.version_info[:2]
    if interactive:
        print(f"  {GREEN}\u2713{NC} Python {major}.{minor} ({python})")
    else:
        ui.info(f"Python {major}.{minor} ({python})")

    # Detect already-installed extras via runtime imports
    prior_extras: dict[str, bool] = _detect_installed_extras(python) if is_reinstall else {}
    # Also honour config-level feature flags (user's prior choice persists
    # even if the venv was recreated or packages temporarily missing)
    if prior_config.get("embeddings"):
        prior_extras.setdefault("ai", True)
    if prior_config.get("sqlite_vec"):
        prior_extras.setdefault("sqlite_vec", True)

    # Feature selection (interactive: prompt now; script: already resolved)
    if interactive:
        install_ai, install_vec = phase_prompt_features(
            ui,
            install_ai,
            install_vec,
            prior_extras=prior_extras,
        )

    install_ai = bool(install_ai)
    install_vec = bool(install_vec)
    has_extras = install_ai or install_vec
    has_config = interactive or args.name or args.api_key or bool(prior_config)

    # ── Step count (stable from here on) ─────────────────────────────
    total = 3  # extract + install + project-setup
    if has_extras:
        total += 1
    if has_config:
        total += 1

    # Create temp dir for wheel extraction
    tmpdir = Path(tempfile.mkdtemp(prefix="trw-install-"))
    try:
        step = 0

        # Step 1: Extract wheels
        step += 1
        memory_whl, mcp_whl = phase_extract_wheels(ui, step, total, tmpdir)

        # Step 2: Install base packages
        step += 1
        phase_install_packages(ui, step, total, python, memory_whl, mcp_whl, pip_target=args.pip_target)

        # Step 3 (conditional): Install extras
        features: list[str] = []
        if has_extras:
            step += 1
            features = phase_install_extras(
                ui,
                step,
                total,
                python,
                install_ai,
                install_vec,
                pip_target=args.pip_target,
            )

        # Step N: Project setup
        step += 1
        phase_project_setup(
            ui,
            step,
            total,
            python,
            target_dir,
            args.upgrade,
            interactive=interactive,
            ide=args.ide,
            pip_target=args.pip_target,
        )

        # Step N+1 (conditional): Configure
        platform_status = "offline"
        if has_config:
            step += 1
            platform_status = phase_configure(
                ui,
                step,
                total,
                target_dir,
                interactive,
                args.name,
                args.api_key,
                args.telemetry,
                prior_config=prior_config,
                install_ai=install_ai,
                install_vec=install_vec,
                skip_auth=getattr(args, "skip_auth", False),
            )

        # Post-install: restart MCP servers and health-check backends
        _restart_mcp_servers(target_dir, ui)

        backend_results: list[dict[str, object]] = []
        if (
            platform_status in ("connected", "telemetry")
            or (target_dir / "docker-compose.yml").is_file()
            or (target_dir / "docker-compose.yaml").is_file()
        ):
            if interactive:
                ui.start_spinner("Checking backend connectivity...")
            backend_results = _check_all_backends(target_dir, prior_config)
            if interactive:
                ui.stop_spinner(True, "Backend connectivity checked")

        # Done!
        show_success_banner(
            ui,
            platform_status,
            features,
            is_reinstall=is_reinstall,
            backend_results=backend_results,
        )

    finally:
        try:
            shutil.rmtree(tmpdir)
        except OSError:
            pass


if __name__ == "__main__":
    main()

