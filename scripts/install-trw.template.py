def _open_tty():  # noqa: ANN202 — returns TextIO | None
    """Open a TTY for reading, or None if unavailable.

    Uses /dev/tty on Unix and CON on Windows.
    """
    for tty_path in ("/dev/tty", "CON"):
        try:
            return open(tty_path)  # noqa: SIM115
        except OSError:
            continue
    return None


def prompt_yes_no(question: str, default: str = "n") -> bool:
    """Prompt for yes/no. Returns True for yes."""
    hint = "[Y/n]" if default == "y" else "[y/N]"
    tty = _open_tty()
    if tty is None:
        return default == "y"
    try:
        sys.stdout.write(f"    {question} {hint} ")
        sys.stdout.flush()
        answer = tty.readline().strip() or default
    finally:
        tty.close()
    return answer.lower().startswith("y")


def prompt_input(prompt_text: str, default: str = "") -> str:
    """Prompt for text input."""
    display = f"    {prompt_text}"
    if default:
        display += f" [{default}]"
    display += " "
    tty = _open_tty()
    if tty is None:
        return default
    try:
        sys.stdout.write(display)
        sys.stdout.flush()
        answer = tty.readline().strip() or default
    finally:
        tty.close()
    return answer


def prompt_choice(label: str, options: list[str], default: int = 0) -> int:
    """Prompt user to select from numbered options. Returns index."""
    print(f"    {label}")
    for i, opt in enumerate(options):
        marker = f"{BOLD}>{NC}" if i == default else " "
        print(f"    {marker} {i + 1}. {opt}")
    tty = _open_tty()
    if tty is None:
        return default
    try:
        sys.stdout.write(f"    Choice [{default + 1}]: ")
        sys.stdout.flush()
        raw = tty.readline().strip()
    finally:
        tty.close()
    if not raw:
        return default
    try:
        choice = int(raw) - 1
        if 0 <= choice < len(options):
            return choice
    except ValueError:
        pass
    return default


