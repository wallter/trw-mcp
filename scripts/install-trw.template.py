def phase_project_setup(
    ui: UI,
    step: int,
    total: int,
    python: str,
    target_dir: Path,
    upgrade_only: bool,
    interactive: bool = False,
    ide: str | None = None,
    pip_target: str = "",
) -> None:
    """Set up or update the project scaffolding."""
    ui.step_header(step, total, "Setting up project")

    if upgrade_only:
        ui.step_ok("Upgrade complete — skipping project setup")
        return

    if not (target_dir / ".git").is_dir():
        ui.step_warn("Not a git repository — skipping project setup")
        ui.step_warn("After git init, run: trw-mcp init-project .")
        return

    # IDE detection and selection
    detected_clis = _detect_installed_clis()
    detected_ides = _detect_project_ides(str(target_dir))

    resolved_ide: str | None = ide
    is_update = (target_dir / ".trw").is_dir()
    if resolved_ide is None:
        if is_update and interactive and detected_ides:
            # Re-run: use previously configured IDEs without prompting
            resolved_ide = "all" if len(detected_ides) >= 2 else detected_ides[0]
            ui.step_ok(f"IDE config: {', '.join(detected_ides)} (from prior install)")
        elif interactive:
            resolved_ide = _prompt_ide_selection(detected_clis, detected_ides)
        else:
            # Headless: auto-configure all detected CLIs; default to claude-code if none
            all_detected = list(dict.fromkeys(detected_clis + detected_ides))
            if len(all_detected) >= 2:
                resolved_ide = "all"
            elif len(all_detected) == 1:
                resolved_ide = all_detected[0]
            else:
                resolved_ide = "claude-code"

    trw_cmd = find_trw_cmd(python, pip_target=pip_target)
    action = "update-project" if is_update else "init-project"
    label = "Updating" if is_update else "Initializing"

    cmd = trw_cmd + [action, str(target_dir)]
    if resolved_ide is not None:
        cmd += ["--ide", resolved_ide]

    ok = run_with_progress(ui, f"{label} project...", cmd)
    ui.stop_spinner(ok, f"Project {'updated' if is_update else 'initialized'}", f"Project {action} failed")

    # Generate per-client instruction files after project setup
    # (FR01, FR02, FR03: Split AGENTS.md into per-client instruction files)
    if resolved_ide in ("all", "opencode", "codex"):
        _generate_per_client_instructions(
            ui,
            target_dir,
            resolved_ide,
            is_update,
            python,
            pip_target=pip_target,
        )


def phase_configure(
    ui: UI,
    step: int,
    total: int,
    target_dir: Path,
    interactive: bool,
    opt_name: str,
    opt_api_key: str,
    opt_telemetry: bool | None,
    prior_config: dict[str, object] | None = None,
    *,
    install_ai: bool = False,
    install_vec: bool = False,
    skip_auth: bool = False,
) -> str:
    """Configure project identity, API key, and telemetry. Returns platform_status."""
    ui.step_header(step, total, "Configure your project")

    if prior_config is None:
        prior_config = {}

    default_name = sanitize_project_name(target_dir.name)
    project_name = ""
    api_key = ""
    telemetry_enabled = False

    if interactive:
        if prior_config.get("project_name"):
            project_name = str(prior_config["project_name"])
            ui.step_ok(f"Project: {project_name} (from prior install)")
        else:
            draw_divider("Project Identity")
            print()
            ui.hint("This name identifies your installation in the TRW platform.")
            ui.hint("It appears in your analytics dashboard and team views.")
            project_name = _prompt_project_name(ui, default_name)

        if prior_config.get("api_key"):
            ui.step_ok("API key: configured (from prior install)")
            api_key = str(prior_config["api_key"])
            telemetry_enabled = True
        elif skip_auth and opt_api_key and validate_api_key(opt_api_key):
            api_key = opt_api_key
            telemetry_enabled = True
            ui.step_ok("API key: accepted (from bootstrap)")
        elif skip_auth:
            ui.step_ok("Auth skipped (--skip-auth)")
        else:
            draw_divider("Platform Connection")
            print()
            ui.hint("Connect to trwframework.com to sync learnings across")
            ui.hint("machines, view analytics, and collaborate with your team.")
            ui.doc_link("integration#platform-connection")
            api_key = _prompt_api_key(ui)

            if api_key:
                telemetry_enabled = True
            elif "telemetry" in prior_config:
                telemetry_enabled = bool(prior_config["telemetry"])
                ui.step_ok(f"Telemetry: {'enabled' if telemetry_enabled else 'disabled'} (from prior install)")
            else:
                print()
                ui.hint("Anonymous telemetry helps us improve TRW for everyone.")
                ui.hint("Only tool usage counts are shared — no code or learnings.")
                ui.doc_link("integration#telemetry")
                telemetry_enabled = prompt_yes_no("Enable anonymous usage telemetry?")
                ui.step_ok("Telemetry enabled" if telemetry_enabled else "Telemetry disabled")
    else:
        # Script mode: use flags
        project_name = sanitize_project_name(opt_name) if opt_name else default_name
        # PRD-FIX-067-FR02: Check prior_config before opt_api_key
        if prior_config.get("api_key"):
            api_key = str(prior_config["api_key"])
            telemetry_enabled = True
            ui.step_ok("API key: configured (from prior install)")
        elif opt_api_key:
            if validate_api_key(opt_api_key):
                api_key = opt_api_key
                telemetry_enabled = True
            else:
                ui.warn("Invalid API key format — ignoring --api-key")
        if not api_key and opt_telemetry is True:
            telemetry_enabled = True
        if opt_telemetry is False:
            telemetry_enabled = False
        ui.step_ok(f"Project: {project_name}")

    # Write to config (including feature flags for future reinstall detection)
    config_path = target_dir / ".trw" / "config.yaml"
    if update_config(
        config_path,
        project_name,
        api_key,
        telemetry_enabled,
        embeddings_enabled=install_ai or None,
        sqlite_vec_enabled=install_vec or None,
    ):
        ui.step_ok("Configuration saved to .trw/config.yaml")
    else:
        ui.step_warn("Config file not found — run project setup first")

    if api_key:
        return "connected"
    if telemetry_enabled:
        return "telemetry"
    return "offline"


def _prompt_project_name(ui: UI, default: str) -> str:
    """Interactive project name prompt with sanitization."""
    raw = prompt_input("Project name:", default)
    name = sanitize_project_name(raw)
    if not name:
        name = default
    if not re.match(r"^[a-z0-9]([a-z0-9-]*[a-z0-9])?$", name):
        ui.step_warn("Could not sanitize name, using default")
        name = default
    if name != raw and raw:
        ui.step_ok(f"Project name: {name} (sanitized from '{raw}')")
    else:
        ui.step_ok(f"Project name: {name}")
    return name


def _prompt_api_key(ui: UI) -> str:
    """Interactive API key prompt -- tries device auth first, falls back to manual paste.

    Device authorization (RFC 8628) opens a browser for passwordless login.
    If device auth is unavailable or user declines, falls back to manual key entry.
    """
    # Try device auth flow first
    try:
        result = _device_auth_login(DOCS_BASE.replace("/docs", ""), interactive=True)
        if result and isinstance(result.get("api_key"), str) and result["api_key"]:
            key = str(result["api_key"])
            if validate_api_key(key):
                ui.step_ok("Authenticated via device flow")
                return key
    except Exception:  # device auth is best-effort, fall back to manual
        pass

    # Fallback: manual key entry
    print()
    ui.hint("Or paste an API key manually:")
    max_attempts = 3
    for attempt in range(max_attempts):
        raw = prompt_input("Platform API key (Enter to skip):").strip()
        if not raw:
            ui.step_ok("No API key -- offline mode")
            return ""
        if validate_api_key(raw):
            ui.step_ok("API key accepted")
            return raw
        remaining = max_attempts - attempt - 1
        if remaining > 0:
            ui.step_warn("Invalid format (must start with trw_, alphanumeric only). Try again:")
        else:
            ui.step_warn("Invalid format -- skipping API key")
    return ""


def _device_auth_login(api_url: str, interactive: bool = True) -> dict | None:
    """Perform RFC 8628 device authorization -- stdlib only.

    Inline implementation that avoids importing trw_mcp.cli.auth (which may
    not be installed when running the standalone installer script).
    """
    import json as _json
    import time as _time
    import urllib.error
    import urllib.request
    import webbrowser as _wb

    api_url = api_url.rstrip("/")

    # Step 1: Request device code
    try:
        body = _json.dumps({"client_id": "trw-cli"}).encode("utf-8")
        req = urllib.request.Request(
            f"{api_url}/v1/auth/device/code",
            data=body,
            method="POST",
            headers={"Content-Type": "application/json"},
        )
        with urllib.request.urlopen(req, timeout=10) as resp:
            code_resp = _json.loads(resp.read().decode("utf-8"))
    except (urllib.error.URLError, OSError):
        return None

    device_code = code_resp.get("device_code", "")
    user_code = code_resp.get("user_code", "")
    verification_uri = code_resp.get("verification_uri", "")
    verification_uri_complete = code_resp.get("verification_uri_complete", verification_uri)
    interval = int(str(code_resp.get("interval", 5)))
    expires_in = int(str(code_resp.get("expires_in", 900)))

    if not device_code or not user_code:
        return None

    # Step 2: Display URL immediately, then try browser in background thread
    if interactive:
        print()
        print(f"  {BOLD}Open this URL to authenticate:{NC}")
        print()
        print(f"    {GREEN}{verification_uri_complete}{NC}")
        print()

        def _open_bg() -> None:
            try:
                _wb.open(verification_uri_complete)
            except Exception:
                pass

        _t = threading.Thread(target=_open_bg, daemon=True)
        _t.start()
        _t.join(timeout=2.0)
        if not _t.is_alive():
            print(f"  {DIM}(Browser opened \u2014 verify the code matches){NC}")
        else:
            print(f"  {DIM}(Opening browser...){NC}")

    # Step 3: Poll for token
    deadline = _time.monotonic() + expires_in
    poll_interval = interval

    while _time.monotonic() < deadline:
        _time.sleep(poll_interval)
        try:
            body = _json.dumps(
                {
                    "client_id": "trw-cli",
                    "device_code": device_code,
                    "grant_type": "urn:ietf:params:oauth:grant-type:device_code",
                }
            ).encode("utf-8")
            req = urllib.request.Request(
                f"{api_url}/v1/auth/device/token",
                data=body,
                method="POST",
                headers={"Content-Type": "application/json"},
            )
            with urllib.request.urlopen(req, timeout=10) as resp:
                return _json.loads(resp.read().decode("utf-8"))
        except urllib.error.HTTPError as exc:
            err_code = ""
            try:
                err_body = _json.loads(exc.read().decode("utf-8"))
                err_code = err_body.get("error", "")
            except Exception:
                pass
            if err_code == "authorization_pending":
                continue
            elif err_code == "slow_down":
                poll_interval += 5
                continue
            elif err_code in ("expired_token", "access_denied"):
                return None
            else:
                return None
        except (urllib.error.URLError, OSError):
            poll_interval = min(poll_interval * 2, 30)
            continue

    return None


# ── Per-client instruction file generation ────────────────────────────


def _generate_per_client_instructions(
    ui: UI,
    target_dir: Path,
    ide_selection: str,
    is_update: bool,
    python: str,
    pip_target: str = "",
) -> None:
    """Generate per-client instruction files for selected IDEs.

    Calls the TRW bootstrap functions to generate INSTRUCTIONS.md files
    optimized for each selected IDE's model family (qwen, gpt, claude).

    FR01, FR02, FR03: Split AGENTS.md into per-client instruction files
    FR06, FR07: Wire per-client instruction generation into installer
    """
    import subprocess

    trw_cmd = find_trw_cmd(python, pip_target=pip_target) + ["update-project", str(target_dir)]

    if ide_selection == "all":
        # Generate for all supported IDEs
        trw_cmd.extend(["--ide", "all"])
    else:
        trw_cmd.extend(["--ide", ide_selection])

    try:
        result = subprocess.run(
            trw_cmd,
            capture_output=True,
            text=True,
            timeout=60,
        )
        if result.returncode == 0:
            if is_update:
                ui.step_ok("Per-client instruction files updated")
            else:
                ui.step_ok("Per-client instruction files generated")
        else:
            ui.step_warn("Instruction file generation encountered warnings")
    except subprocess.TimeoutExpired:
        ui.step_warn("Instruction file generation timed out (may still be running)")
    except Exception as exc:
        ui.step_warn(f"Failed to generate per-client instruction files: {exc}")


