# ── Wheel extraction ─────────────────────────────────────────────────


def _extract_wheel_data(marker: str) -> bytes:
    """Read base64 data following *marker* from this script file.

    Wheels are embedded as comment lines after marker comments at the
    end of the file (e.g. MEMORY_WHEEL_DATA, WHEEL_DATA). Each marker
    is followed by comment-prefixed base64 lines.
    """
    script_path = Path(sys.argv[0]).resolve() if sys.argv[0] != "-" else Path(__file__).resolve()
    text = script_path.read_text(encoding="utf-8", errors="replace")

    collecting = False
    b64_chunks: list[str] = []

    for line in text.splitlines():
        stripped = line.strip()
        if stripped == f"# {marker}":
            collecting = True
            continue
        if collecting:
            if stripped.startswith("# __") and stripped.endswith("__"):
                break
            if stripped.startswith("# "):
                b64_chunks.append(stripped[2:])
            elif stripped == "#":
                continue
            else:
                break

    if not b64_chunks:
        raise RuntimeError(f"No embedded data found for marker {marker}")

    return base64.b64decode("".join(b64_chunks))


def _verify_checksum(data: bytes, expected_sha256: str, label: str) -> None:
    """Verify SHA-256 checksum of extracted wheel data.

    Skips verification if expected_sha256 is empty or a template placeholder.
    """
    if not expected_sha256 or expected_sha256.startswith("{{"):
        return
    import hashlib

    actual = hashlib.sha256(data).hexdigest()
    if actual != expected_sha256:
        raise RuntimeError(
            f"Checksum mismatch for {label}: "
            f"expected {expected_sha256[:16]}..., got {actual[:16]}..."
        )


def extract_wheels(tmpdir: Path) -> tuple[Path, Path]:
    """Extract all embedded wheels into *tmpdir*.

    Returns (memory_wheel_path, mcp_wheel_path).
    Verifies SHA-256 checksums when available (set by build_installer.py).
    """
    memory_data = _extract_wheel_data("__MEMORY_WHEEL_DATA__")
    _verify_checksum(memory_data, MEMORY_WHEEL_SHA256, MEMORY_WHEEL_FILENAME)
    memory_path = tmpdir / MEMORY_WHEEL_FILENAME
    memory_path.write_bytes(memory_data)

    mcp_data = _extract_wheel_data("__WHEEL_DATA__")
    _verify_checksum(mcp_data, WHEEL_SHA256, WHEEL_FILENAME)
    mcp_path = tmpdir / WHEEL_FILENAME
    mcp_path.write_bytes(mcp_data)

    return memory_path, mcp_path


# ── pip install with fallback ────────────────────────────────────────


def _pip_target_from_cmd(cmd: list[str]) -> str:
    """Extract ``--target DIR`` from a pip command when present."""
    for index, part in enumerate(cmd):
        if part == "--target" and index + 1 < len(cmd):
            return cmd[index + 1]
    return ""


def _build_pip_runtime_env(target_dir: str = "") -> dict[str, str]:
    """Build env that keeps pip cache/temp writes inside the tmpfs target."""
    env = dict(os.environ)
    env["PYTHONDONTWRITEBYTECODE"] = "1"
    if not target_dir:
        return env

    target_path = Path(target_dir)
    cache_root = target_path / ".cache"
    pip_cache_dir = cache_root / "pip"
    tmp_dir = target_path / ".tmp"
    pip_cache_dir.mkdir(parents=True, exist_ok=True)
    tmp_dir.mkdir(parents=True, exist_ok=True)
    env["PIP_NO_CACHE_DIR"] = "1"
    env["PIP_CACHE_DIR"] = str(pip_cache_dir)
    env["XDG_CACHE_HOME"] = str(cache_root)
    env["TMPDIR"] = str(tmp_dir)
    return env


def _run_quiet(cmd: list[str], timeout: int = 120) -> bool:
    """Run a command silently, return True if exit code == 0.

    *timeout* (seconds, default 120) prevents hangs when pip stalls on
    PEP 668 externally-managed system Pythons without a venv.

    ``KeyboardInterrupt`` is intentionally not caught — it propagates to
    the caller so the user can abort the entire installer with Ctrl-C.
    """
    try:
        env = _build_pip_runtime_env(_pip_target_from_cmd(cmd))
        return (
            subprocess.run(
                cmd,
                env=env,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                timeout=timeout,
            ).returncode
            == 0
        )
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return False


def _wheel_runtime_dependencies_satisfied(wheel_path: Path) -> bool:
    """Return True when installed packages already satisfy wheel dependencies."""
    try:
        with zipfile.ZipFile(wheel_path) as wheel:
            metadata_name = next(name for name in wheel.namelist() if name.endswith(".dist-info/METADATA"))
            metadata = BytesParser().parsebytes(wheel.read(metadata_name))
    except (OSError, StopIteration, zipfile.BadZipFile, KeyError):
        return False

    for requirement_text in metadata.get_all("Requires-Dist", []):
        try:
            requirement = Requirement(requirement_text)
        except Exception:
            return False
        if requirement.marker and not requirement.marker.evaluate({"extra": ""}):
            continue
        try:
            installed_version = importlib_metadata.version(requirement.name)
        except importlib_metadata.PackageNotFoundError:
            return False
        if requirement.specifier and not requirement.specifier.contains(installed_version, prereleases=True):
            return False
    return True


def pip_install(python: str, package: str, label: str, ui: UI, target_dir: str = "") -> bool:
    """Try pip install with escalating fallbacks.

    When *target_dir* is set, passes ``--target DIR`` to pip so packages
    are written to that directory instead of site-packages.  This is used
    to redirect writes to a tmpfs mount (PRD-INFRA-058).

    Tries: normal -> --user -> --break-system-packages.
    """
    base = [python, "-B", "-m", "pip", "install", "--upgrade", "--quiet"]
    if target_dir:
        if package.endswith(".whl") and _wheel_runtime_dependencies_satisfied(Path(package)):
            base.append("--no-deps")
        base += ["--target", target_dir]

    if _run_quiet(base + [package]):
        return True

    if not target_dir and _run_quiet(base + ["--user", package]):
        ui.step_warn(f"Installed {label} with --user (PEP 668 managed environment)")
        return True

    if _run_quiet(base + ["--break-system-packages", package]):
        ui.step_warn(f"Installed {label} with --break-system-packages")
        return True

    return False


def validate_pip_target(target_dir: str) -> str:
    """Validate --pip-target to reject shell metacharacters and malformed paths."""
    if not target_dir:
        return ""
    if not re.fullmatch(r"[A-Za-z0-9_./-]+", target_dir):
        raise ValueError(f"Invalid --pip-target: {target_dir!r}")
    return target_dir


# ── Run command with live progress ───────────────────────────────────


def run_with_progress(ui: UI, fallback_msg: str, cmd: list[str], timeout: int = 180) -> bool:
    """Run *cmd* showing live progress. Returns True on success.

    A watchdog timer kills the subprocess if it hasn't exited after *timeout*
    seconds (default 180).  This prevents the installer from hanging
    indefinitely when the child stalls (e.g. during CLAUDE.md sync).
    """
    try:
        proc = subprocess.Popen(
            cmd,
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
        )
    except FileNotFoundError:
        return False

    assert proc.stdout is not None

    killed_by_watchdog = False

    def _watchdog_kill() -> None:
        nonlocal killed_by_watchdog
        killed_by_watchdog = True
        proc.kill()

    watchdog = threading.Timer(timeout, _watchdog_kill)
    watchdog.daemon = True
    watchdog.start()

    try:
        if ui.interactive:
            ui.start_spinner(fallback_msg)
            file_count = 0
            for line in proc.stdout:
                line = line.strip()
                # Phase markers update the status message (e.g., "Phase: Syncing CLAUDE.md...")
                if line.startswith("Phase:"):
                    phase_label = line.partition(":")[2].strip()
                    ui.update_spinner(f"{fallback_msg} ({file_count} files) {phase_label}")
                elif re.match(r"  *(Updated|Created|Preserved|Skipped|synced|WARNING|Error):? ", line):
                    file_count += 1
                    short = re.sub(r"^  *(Updated|Created|Created \(new\)|Preserved|Skipped|Error): ", "", line)[:60]
                    ui.update_spinner(f"{fallback_msg} ({file_count} files) {DIM}{short}{NC}")
        else:
            for line in proc.stdout:
                if not ui.quiet:
                    print(f"{GREEN}[TRW]{NC}   {line.rstrip()}")

        proc.wait(timeout=10)
    except subprocess.TimeoutExpired:
        proc.kill()
        proc.wait()
    finally:
        watchdog.cancel()

    if killed_by_watchdog:
        ui.step_warn(f"{fallback_msg} timed out after {timeout}s")

    return proc.returncode == 0


# ── Config file update ───────────────────────────────────────────────


def update_config(
    config_path: Path,
    project_name: str,
    api_key: str,
    telemetry_enabled: bool,
    *,
    embeddings_enabled: bool | None = None,
    sqlite_vec_enabled: bool | None = None,
) -> bool:
    """Update .trw/config.yaml with installation settings.

    Optional *embeddings_enabled* and *sqlite_vec_enabled* persist feature
    flags so reinstalls skip the "Optional Features" prompts.
    """
    if not config_path.is_file():
        return False

    lines = config_path.read_text(encoding="utf-8").splitlines(keepends=True)
    platform_url = "https://api.trwframework.com"

    updated: set[str] = set()
    out: list[str] = []
    replacing_platform_urls = False

    for line in lines:
        normalized_line = line if line.endswith("\n") else line + "\n"
        s = normalized_line.lstrip()

        if replacing_platform_urls:
            if s.startswith("- "):
                continue
            replacing_platform_urls = False

        if s.startswith("installation_id:"):
            out.append(f"installation_id: {project_name}\n")
            updated.add("installation_id")
            continue
        if s.startswith("platform_api_key:"):
            out.append(f'platform_api_key: "{api_key}"\n' if api_key else line)
            updated.add("platform_api_key")
            continue
        if s.startswith("platform_telemetry_enabled:"):
            val = "true" if telemetry_enabled else "false"
            out.append(f"platform_telemetry_enabled: {val}\n")
            updated.add("platform_telemetry_enabled")
            continue
        if s.startswith("embeddings_enabled:") and embeddings_enabled is not None:
            out.append(f"embeddings_enabled: {'true' if embeddings_enabled else 'false'}\n")
            updated.add("embeddings_enabled")
            continue
        if s.startswith("sqlite_vec_enabled:") and sqlite_vec_enabled is not None:
            out.append(f"sqlite_vec_enabled: {'true' if sqlite_vec_enabled else 'false'}\n")
            updated.add("sqlite_vec_enabled")
            continue
        if s.startswith("platform_urls:"):
            updated.add("platform_urls")
            if api_key or telemetry_enabled:
                out.append("platform_urls:\n")
                out.append(f'  - "{platform_url}"\n')
                updated.add("platform_urls_written")
                replacing_platform_urls = True
                continue
            out.append(normalized_line)
            continue

        # Skip commented-out platform config hints
        skip_prefixes = (
            "# platform_urls:",
            '#   - "https://api.',
            "# platform_api_key:",
            "# platform_telemetry_enabled:",
            "# Platform telemetry",
        )
        if any(s.startswith(p) for p in skip_prefixes):
            continue

        out.append(normalized_line)

    # Append missing keys
    if "installation_id" not in updated:
        out.append(f"installation_id: {project_name}\n")
    if api_key and "platform_api_key" not in updated:
        out.append(f'platform_api_key: "{api_key}"\n')
    if telemetry_enabled and "platform_telemetry_enabled" not in updated:
        out.append("platform_telemetry_enabled: true\n")
    if (api_key or telemetry_enabled) and "platform_urls_written" not in updated:
        out.append("platform_urls:\n")
        out.append(f'  - "{platform_url}"\n')
    if embeddings_enabled and "embeddings_enabled" not in updated:
        out.append("embeddings_enabled: true\n")
    if sqlite_vec_enabled and "sqlite_vec_enabled" not in updated:
        out.append("sqlite_vec_enabled: true\n")

    config_path.write_text("".join(out), encoding="utf-8")
    return True


# ── Health check ─────────────────────────────────────────────────


def _check_backend_health(url: str, timeout: float = 5.0) -> dict[str, object]:
    """Probe a backend health endpoint. Returns status dict.

    Uses only stdlib — no external dependencies.
    """
    import json
    import urllib.request
    import urllib.error

    health_url = f"{url.rstrip('/')}/v1/health"
    try:
        req = urllib.request.Request(health_url, method="GET")
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            data = json.loads(resp.read().decode("utf-8"))
            return {"url": url, "reachable": True, "status": data.get("status", "ok")}
    except urllib.error.HTTPError as exc:
        return {"url": url, "reachable": True, "status": f"http-{exc.code}"}
    except Exception:
        return {"url": url, "reachable": False, "status": "unreachable"}


def _check_all_backends(
    target_dir: Path,
    prior_config: dict[str, object],
) -> list[dict[str, object]]:
    """Check connectivity to all configured platform backends.

    Reads platform_urls from config.yaml (simple line parsing) and probes
    each one. Also checks localhost:8000 if a docker-compose.yml exists
    in the project (indicating a local backend may be running).
    """
    results: list[dict[str, object]] = []
    urls: list[str] = []

    # Read platform_urls from config.yaml
    config_path = target_dir / ".trw" / "config.yaml"
    if config_path.is_file():
        try:
            in_urls = False
            for line in config_path.read_text(encoding="utf-8").splitlines():
                stripped = line.strip()
                if stripped.startswith("platform_urls:"):
                    in_urls = True
                    continue
                if in_urls:
                    if stripped.startswith("- "):
                        url = stripped[2:].strip().strip('"').strip("'")
                        if url:
                            urls.append(url)
                    elif stripped and not stripped.startswith("#"):
                        in_urls = False
        except OSError:
            pass

    # Check for local Docker backend
    if (target_dir / "docker-compose.yml").is_file() or (target_dir / "docker-compose.yaml").is_file():
        local_url = "http://localhost:8000"
        if local_url not in urls:
            urls.insert(0, local_url)

    # Probe all backends in parallel for faster results
    if not urls:
        return results
    if len(urls) == 1:
        return [_check_backend_health(urls[0])]

    from concurrent.futures import ThreadPoolExecutor, as_completed

    with ThreadPoolExecutor(max_workers=min(len(urls), 4)) as pool:
        futures = {pool.submit(_check_backend_health, url): url for url in urls}
        for future in as_completed(futures, timeout=10):
            try:
                results.append(future.result())
            except Exception:
                results.append({"url": futures[future], "reachable": False, "status": "error"})

    # Preserve original URL ordering
    url_order = {url: i for i, url in enumerate(urls)}
    results.sort(key=lambda r: url_order.get(str(r["url"]), 999))
    return results


# ── MCP server restart ───────────────────────────────────────────


def _is_process_alive(pid: int) -> bool:
    """Check if a process with the given PID is running. Cross-platform.

    - Unix/macOS: ``os.kill(pid, 0)`` (signal 0 = existence check).
    - Windows: ``os.kill(pid, 0)`` raises SystemError on Windows
      (CPython issue #14480). Use ``kernel32.OpenProcess`` instead.
    """
    import os

    if sys.platform == "win32":
        try:
            import ctypes

            SYNCHRONIZE = 0x00100000
            handle = ctypes.windll.kernel32.OpenProcess(SYNCHRONIZE, 0, pid)  # type: ignore[union-attr]
            if handle == 0:
                return False
            ctypes.windll.kernel32.CloseHandle(handle)  # type: ignore[union-attr]
            return True
        except (OSError, AttributeError):
            return False
    else:
        try:
            os.kill(pid, 0)
            return True
        except (ProcessLookupError, OSError):
            return False


def _terminate_process(pid: int) -> bool:
    """Terminate a process by PID. Cross-platform.

    - Unix/macOS: sends SIGTERM for graceful shutdown.
    - Windows: ``os.kill(pid, SIGTERM)`` calls ``TerminateProcess``
      (forceful but reliable — no graceful path without
      ``CREATE_NEW_PROCESS_GROUP`` at spawn time). Falls back to
      ``taskkill /PID`` if that fails.

    Returns True if the process was signalled successfully.
    """
    import os
    import signal

    try:
        if sys.platform == "win32":
            # On Windows, os.kill(pid, SIGTERM) calls TerminateProcess —
            # always forceful. No graceful alternative without spawn-time
            # CREATE_NEW_PROCESS_GROUP flag (see uvicorn PR #1909).
            try:
                os.kill(pid, signal.SIGTERM)
                return True
            except OSError:
                # Fallback: taskkill handles more edge cases on Windows
                return _run_quiet(["taskkill", "/PID", str(pid), "/F"])
        else:
            os.kill(pid, signal.SIGTERM)
            return True
    except (ProcessLookupError, PermissionError, OSError):
        return False


def _restart_mcp_servers(target_dir: Path, ui: UI) -> None:
    """Restart MCP server(s) after install/upgrade.

    Cross-platform approach:
      - HTTP mode: kill the background process via PID file; it will be
        auto-started on the next tool call via ``ensure_http_server()``.
      - All modes: write a version sentinel so the server detects the
        upgrade on the next tool call and advises ``/mcp``.
    """
    import json

    trw_dir = target_dir / ".trw"
    pid_path = trw_dir / "mcp-server.pid"

    # HTTP mode: kill the background server process
    if pid_path.is_file():
        try:
            pid = int(pid_path.read_text(encoding="utf-8").strip())
            if _is_process_alive(pid):
                if _terminate_process(pid):
                    ui.step_ok("MCP server stopped (will auto-start on next use)")
                pid_path.unlink(missing_ok=True)
            else:
                # Already dead — clean up stale PID
                pid_path.unlink(missing_ok=True)
        except (ValueError, OSError):
            pid_path.unlink(missing_ok=True)

    # Write version sentinel for stdio instances to detect upgrade
    sentinel_path = trw_dir / "installed-version.json"
    try:
        sentinel_path.write_text(
            json.dumps({"version": TRW_VERSION, "timestamp": _iso_now()}),
            encoding="utf-8",
        )
    except OSError:
        pass  # Best-effort


def _iso_now() -> str:
    """Return current UTC timestamp in ISO 8601 format."""
    from datetime import datetime, timezone

    return datetime.now(timezone.utc).isoformat()


