def _visible_len(text: str) -> int:
    """Return the display width of *text* (strip ANSI escapes)."""
    return len(_ANSI_RE.sub("", text))


# ── Box drawing ──────────────────────────────────────────────────────

def draw_box(
    lines: list[str],
    color: str = CYAN,
    width: int = 52,
    padding: int = 2,
) -> None:
    """Draw a Unicode box with proper alignment.

    *width* is the total box width including border characters.
    *padding* is left margin inside the box.
    """
    inner = width - 2
    hbar = "\u2500" * inner
    tl, tr, bl, br, vb = "\u256d", "\u256e", "\u2570", "\u256f", "\u2502"
    print(f"{color}{tl}{hbar}{tr}{NC}")
    for line in lines:
        vis = _visible_len(line)
        gap = " " * max(0, inner - padding - vis)
        margin = " " * padding
        print(f"{color}{vb}{NC}{margin}{line}{gap}{color}{vb}{NC}")
    print(f"{color}{bl}{hbar}{br}{NC}")


def draw_divider(label: str = "", color: str = DIM) -> None:
    """Print a subtle divider with optional centered label."""
    if label:
        print(f"\n  {color}\u2500\u2500 {label} \u2500\u2500{NC}")
    else:
        print()


# ── Output helpers ───────────────────────────────────────────────────

class UI:
    """Output helper that adapts to interactive/quiet/script modes."""

    def __init__(self, interactive: bool = False, quiet: bool = False) -> None:
        self.interactive = interactive
        self.quiet = quiet
        self._spinner: _Spinner | None = None

    def info(self, msg: str) -> None:
        if not self.quiet:
            print(f"{GREEN}[TRW]{NC} {msg}")

    def warn(self, msg: str) -> None:
        print(f"{YELLOW}[TRW]{NC} {msg}")

    def error(self, msg: str) -> None:
        print(f"{RED}[TRW]{NC} {msg}", file=sys.stderr)

    def blank(self) -> None:
        if not self.quiet:
            print()

    def step_header(self, step: int, total: int, label: str) -> None:
        if self.interactive:
            print(f"\n  {BOLD}Step {step}/{total}{NC}  {label}")
        else:
            self.info(label)

    def step_ok(self, msg: str) -> None:
        if self.interactive:
            print(f"            {GREEN}\u2713{NC} {msg}")
        elif not self.quiet:
            self.info(msg)

    def step_warn(self, msg: str) -> None:
        if self.interactive:
            print(f"            {YELLOW}!{NC} {msg}")
        else:
            self.warn(msg)

    def step_fail(self, msg: str) -> None:
        if self.interactive:
            print(f"            {RED}\u2717{NC} {msg}")
        else:
            self.error(msg)

    def hint(self, msg: str) -> None:
        """Show a dim hint line (interactive only)."""
        if self.interactive:
            print(f"    {DIM}{msg}{NC}")

    def doc_link(self, anchor: str) -> None:
        """Show a docs link hint."""
        self.hint(f"Learn more: {DOCS_BASE}/{anchor}")

    # ── Spinner ──────────────────────────────────────────────────────

    def start_spinner(self, msg: str) -> None:
        if not self.interactive:
            return
        self._spinner = _Spinner(msg)
        self._spinner.start()

    def update_spinner(self, msg: str) -> None:
        if self._spinner is not None:
            self._spinner.message = msg

    def stop_spinner(self, success: bool, ok_msg: str, fail_msg: str = "Failed") -> None:
        if self._spinner is not None:
            self._spinner.stop()
            self._spinner = None
            sys.stdout.write("\r\033[K")
            sys.stdout.flush()
        if success:
            self.step_ok(ok_msg)
        else:
            self.step_fail(fail_msg)


class _Spinner:
    """Braille-character spinner in a daemon thread."""

    _FRAMES = "\u280b\u2819\u2839\u2838\u283c\u2834\u2826\u2827\u2807\u280f"

    def __init__(self, message: str) -> None:
        self.message = message
        self._stop = threading.Event()
        self._thread: threading.Thread | None = None

    def start(self) -> None:
        self._thread = threading.Thread(target=self._run, daemon=True)
        self._thread.start()

    def stop(self) -> None:
        self._stop.set()
        if self._thread is not None:
            self._thread.join(timeout=2.0)

    def _run(self) -> None:
        idx = 0
        while not self._stop.is_set():
            frame = self._FRAMES[idx % len(self._FRAMES)]
            sys.stdout.write(f"\r\033[K            {frame} {self.message}")
            sys.stdout.flush()
            idx += 1
            self._stop.wait(0.1)


# ── Input helpers ────────────────────────────────────────────────────

def _open_tty():  # noqa: ANN202 — returns TextIO | None
    """Open /dev/tty for reading, or None if unavailable."""
    try:
        return open("/dev/tty", "r")  # noqa: SIM115
    except OSError:
        return None


def prompt_yes_no(question: str, default: str = "n") -> bool:
    """Prompt for yes/no. Returns True for yes."""
    hint = "[Y/n]" if default == "y" else "[y/N]"
    tty = _open_tty()
    if tty is None:
        return default == "y"
    try:
        sys.stdout.write(f"    {question} {hint} ")
        sys.stdout.flush()
        answer = tty.readline().strip() or default
    finally:
        tty.close()
    return answer.lower().startswith("y")


def prompt_input(prompt_text: str, default: str = "") -> str:
    """Prompt for text input."""
    display = f"    {prompt_text}"
    if default:
        display += f" [{default}]"
    display += " "
    tty = _open_tty()
    if tty is None:
        return default
    try:
        sys.stdout.write(display)
        sys.stdout.flush()
        answer = tty.readline().strip() or default
    finally:
        tty.close()
    return answer


def prompt_choice(label: str, options: list[str], default: int = 0) -> int:
    """Prompt user to select from numbered options. Returns index."""
    print(f"    {label}")
    for i, opt in enumerate(options):
        marker = f"{BOLD}>{NC}" if i == default else " "
        print(f"    {marker} {i + 1}. {opt}")
    tty = _open_tty()
    if tty is None:
        return default
    try:
        sys.stdout.write(f"    Choice [{default + 1}]: ")
        sys.stdout.flush()
        raw = tty.readline().strip()
    finally:
        tty.close()
    if not raw:
        return default
    try:
        choice = int(raw) - 1
        if 0 <= choice < len(options):
            return choice
    except ValueError:
        pass
    return default


# ── Prior installation detection ─────────────────────────────────────

def _load_prior_config(target_dir: Path) -> dict[str, object]:
    """Load prior installation config from .trw/config.yaml if it exists.

    Returns a dict with keys that were previously configured, or empty dict
    for first-time installs. Parses YAML manually — no pyyaml dependency.
    """
    config_path = target_dir / ".trw" / "config.yaml"
    if not config_path.is_file():
        return {}

    prior: dict[str, object] = {}
    try:
        for line in config_path.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if line.startswith("#") or not line or ":" not in line:
                continue
            key, _, value = line.partition(":")
            key = key.strip()
            value = value.strip().strip('"').strip("'")
            if key == "installation_id":
                prior["project_name"] = value
            elif key == "platform_api_key":
                prior["api_key"] = value
            elif key == "platform_telemetry_enabled":
                prior["telemetry"] = value.lower() == "true"
            elif key == "embeddings_enabled":
                prior["embeddings"] = value.lower() == "true"
    except OSError:
        pass
    return prior


