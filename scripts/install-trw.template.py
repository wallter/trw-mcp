def phase_prompt_features(
    ui: UI,
    install_ai: bool | None,
    install_sqlitevec: bool | None,
    prior_extras: dict[str, bool] | None = None,
) -> tuple[bool, bool]:
    """Prompt for optional features (interactive only). Skips if already answered.

    When both extras are already configured (prior install or CLI flags),
    shows a compact summary line instead of the full prompt section.
    """
    if prior_extras is None:
        prior_extras = {}

    ai_configured = prior_extras.get("ai", False)
    vec_configured = prior_extras.get("sqlite_vec", False)

    # Both already determined via CLI flags — nothing to show
    if install_ai is not None and install_sqlitevec is not None:
        return bool(install_ai), bool(install_sqlitevec)

    # Both from prior install — compact one-liner, no prompts
    if ai_configured and vec_configured and install_ai is None and install_sqlitevec is None:
        names = []
        if ai_configured:
            names.append("AI/LLM")
        if vec_configured:
            names.append("sqlite-vec")
        print(f"  {GREEN}\u2713{NC} Extras: {', '.join(names)} (from prior install)")
        return True, True

    # Need to prompt for at least one — show the feature section
    draw_divider("Optional Features")

    if install_ai is None:
        if ai_configured:
            print(f"  {GREEN}\u2713{NC} AI extras (from prior install)")
            install_ai = True
        else:
            print()
            ui.hint("AI extras enable semantic search over your learnings")
            ui.hint("and LLM-powered analysis of patterns across sessions.")
            ui.doc_link("concepts")
            install_ai = prompt_yes_no("Install AI/LLM features?")

    if install_sqlitevec is None:
        if vec_configured:
            print(f"  {GREEN}\u2713{NC} sqlite-vec (from prior install)")
            install_sqlitevec = True
        else:
            print()
            ui.hint("sqlite-vec adds vector similarity search for faster,")
            ui.hint("more relevant recall of past learnings and discoveries.")
            ui.doc_link("concepts")
            install_sqlitevec = prompt_yes_no("Install sqlite-vec for vector search?")

    return bool(install_ai), bool(install_sqlitevec)


def _verify_package_imports(python: str, validated_target: str, ui: UI) -> dict[str, str]:
    """Verify trw_memory + trw_mcp import after install; exit on failure.

    Extracted (PRD-INFRA-150) so the downgrade-guard early-return path (both
    packages already newer-installed) reuses the same verification the normal
    install path runs — DRY, and proves the kept install is importable. Returns
    the resolved runtime ``env`` so callers (the MCP preflight) reuse it.
    """
    env = _build_pip_runtime_env(validated_target)
    if validated_target:
        current_path = env.get("PYTHONPATH", "")
        env["PYTHONPATH"] = f"{validated_target}:{current_path}" if current_path else validated_target
    for mod in ("trw_memory", "trw_mcp"):
        rc = subprocess.run(
            [python, "-B", "-c", f"import {mod}"],
            env=env,
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
        ).returncode
        if rc != 0:
            ui.step_fail(f"{mod} not importable after install")
            sys.exit(1)
    return env


def phase_install_packages(
    ui: UI,
    step: int,
    total: int,
    python: str,
    memory_whl: Path,
    mcp_whl: Path,
    pip_target: str = "",
    offline: bool = False,
) -> str:
    """Phase 3: Install base packages (order: memory -> mcp).

    Returns the EFFECTIVE interpreter to use for every subsequent phase. This is
    normally *python*, but on a PEP 668 system Python that declined system
    mutation the install falls back to a dedicated venv (see pip_install /
    _ensure_fallback_venv) and this returns that venv's interpreter instead — so
    the caller rebinds ``python`` and downstream phases (extras, project setup,
    the emitted client MCP config) all target the venv where trw_mcp is
    importable.

    When *pip_target* is set, passes ``--target`` to pip so all packages
    are written to that directory (e.g. a tmpfs mount) instead of
    site-packages.  A PYTHONPATH wrapper is generated under
    ``<pip-target>/bin/trw-mcp`` so the CLI finds the packages at runtime
    without writing to overlay-backed system paths.

    PRD-SEC-006-FR09: when *offline* is True, pip is invoked with ``--no-index``
    so it NEVER reaches PyPI — it resolves trw-mcp + trw-memory from the embedded
    wheels (via ``--find-links``) and external transitive deps from already-
    installed packages or a ``--find-links`` directory. Combine with proxy env
    vars (HTTPS_PROXY/HTTP_PROXY/NO_PROXY) for air-gapped hosts.
    """
    ui.step_header(step, total, "Installing packages")
    validated_target = validate_pip_target(pip_target)
    if offline:
        ui.step_warn("Offline mode: installing from embedded wheels only (--no-index)")

    # PRD-INFRA-150 FR03/FR04: downgrade guard. Probe the TARGET interpreter for
    # the already-installed versions and decide, per package, whether the bundled
    # wheel should be force-installed. We only SKIP force-install when the
    # installed version is strictly NEWER than the bundled one (a downgrade).
    # For the bundled-newer / equal / fresh / probe-error cases the decision is
    # "install bundled", keeping behavior byte-identical to before (NFR04).
    # TRW_VERSION is the bundled version for both wheels (built in lockstep).
    bundled_version = TRW_VERSION
    installed_mcp = _probe_installed_version(python, "trw-mcp")
    installed_memory = _probe_installed_version(python, "trw-memory")
    skip_mcp, mcp_decision = downgrade_guard_decision(installed_mcp, bundled_version)
    skip_memory, memory_decision = downgrade_guard_decision(installed_memory, bundled_version)
    ui.info(format_guard_log_line("trw-mcp", installed_mcp, bundled_version, mcp_decision))
    ui.info(format_guard_log_line("trw-memory", installed_memory, bundled_version, memory_decision))

    # Build the combined wheel list, excluding any package whose installed
    # version is strictly newer (the downgrade case). When nothing is skipped,
    # this is the exact [memory, mcp] list as before (NFR04 byte-identical).
    combined_wheels: list[str] = []
    if not skip_memory:
        combined_wheels.append(str(memory_whl))
    if not skip_mcp:
        combined_wheels.append(str(mcp_whl))
    if not combined_wheels:
        # Both newer-installed: nothing to force-install. Verify imports and exit
        # the install step without mutating the newer site-packages (RISK-006).
        ui.info("Downgrade guard: keeping newer installed trw-mcp + trw-memory; nothing to install")
        _verify_package_imports(python, validated_target, ui)
        return python

    # 2026-04-21 L-8heG v2: install BOTH bundled wheels in one pip invocation
    # with --find-links pointing at the wheel directory. This lets pip's
    # resolver:
    #   (a) satisfy the trw-mcp -> trw-memory internal dep from the bundled
    #       wheel (even when the bundled version is ahead of PyPI), AND
    #   (b) fetch external transitive deps (structlog, pydantic, ruamel-yaml,
    #       pydantic-settings) from PyPI normally.
    # Previous sequential approach broke on either side — installing
    # trw-mcp alone caused pip to miss the locally-bundled trw-memory and
    # fail on PyPI; forcing --no-deps solved that but then skipped external
    # deps entirely, crashing trw-mcp on `import structlog` in the container.
    wheel_dir = memory_whl.parent
    combined_cmd = build_install_cmd(
        python,
        ui,
        combined_wheels,
        target_dir=validated_target,
        find_links=str(wheel_dir),
        no_index=offline,
    )

    ui.start_spinner(f"Installing trw-memory + trw-mcp v{TRW_VERSION}...")
    install_env = _build_pip_runtime_env(validated_target)
    result = subprocess.run(
        combined_cmd,
        env=install_env,
        capture_output=True,
        text=True,
        timeout=300,
    )
    if result.returncode != 0:
        # Fall back to the sequential path so PEP 668 / environment-specific
        # failures still get the --user and --break-system-packages escalation.
        ui.stop_spinner(
            False,
            "",
            f"combined install failed (rc={result.returncode}), trying sequential",
        )
        if not skip_memory and not pip_install(
            python, str(memory_whl), "trw-memory", ui, target_dir=validated_target
        ):
            ui.error("pip install failed for trw-memory")
            ui.error("Try a clean environment or force the uv backend:")
            ui.error("  python3 -m venv .venv && source .venv/bin/activate && python3 install-trw.py")
            ui.error("  (uv-managed Python) TRW_INSTALL_BACKEND=uv python3 install-trw.py")
            sys.exit(1)
        if not skip_mcp and not pip_install(
            python, str(mcp_whl), "trw-mcp", ui, target_dir=validated_target
        ):
            ui.error("pip install failed for trw-mcp")
            ui.error("Try a clean environment or force the uv backend:")
            ui.error("  python3 -m venv .venv && source .venv/bin/activate && python3 install-trw.py")
            ui.error("  (uv-managed Python) TRW_INSTALL_BACKEND=uv python3 install-trw.py")
            sys.exit(1)
    ui.stop_spinner(True, f"Installed trw-memory + trw-mcp v{TRW_VERSION}")

    # If the sequential fallback landed in a dedicated venv (PEP 668 system
    # Python, system mutation declined), rebind to that interpreter so the
    # force-pin, import verification, and every downstream phase target the venv
    # where trw_mcp/trw_memory are actually importable.
    effective_python = _FALLBACK_VENV_PYTHON or python
    if _FALLBACK_VENV_PYTHON:
        ui.info(f"Using isolated venv interpreter for the rest of setup: {effective_python}")

    # Force-reinstall trw-memory from bundled wheel (defeats PyPI downgrade).
    # Rationale: trw-mcp's install triggers pip's dep resolver which reaches
    # PyPI for trw-memory and may downgrade our bundled version if PyPI lags
    # behind local builds. Force-reinstall with --no-deps guarantees the
    # bundled wheel wins. --no-cache-dir bypasses any pip cache that could
    # hold a stale PyPI copy.
    # pip --target has a quirk: even with --force-reinstall, it doesn't
    # overwrite existing directories (it just warns and skips). So we
    # manually delete the package dirs first, then reinstall.
    # PRD-INFRA-150 FR03: skip the force-pin entirely when a strictly-newer
    # trw-memory is already installed — re-pinning would downgrade it. This is a
    # no-op for the bundled-newer / equal / fresh cases (NFR04 byte-identical).
    if skip_memory:
        ui.info("Downgrade guard: keeping newer installed trw-memory; skip bundled-wheel pin")
    else:
        if validated_target:
            target_path = Path(validated_target)
            for pattern in ("trw_memory", "trw_memory-*.dist-info"):
                for p in target_path.glob(pattern):
                    if p.is_dir():
                        shutil.rmtree(p, ignore_errors=True)
        cmd = build_install_cmd(
            effective_python,
            ui,
            [str(memory_whl)],
            target_dir=validated_target,
            no_deps=True,
            no_cache=True,
            no_index=True,
        )
        result = subprocess.run(
            cmd,
            env=install_env,
            capture_output=True,
            text=True,
            timeout=120,
        )
        if result.returncode != 0:
            ui.step_warn(f"trw-memory pin failed: {result.stderr[:200]}")
        else:
            ui.info(f"Pinned trw-memory to bundled wheel ({memory_whl.name})")

    # When using --pip-target, generate a PYTHONPATH wrapper (FR04)
    if validated_target:
        _write_pythonpath_wrapper(
            Path(validated_target) / "bin", "trw-mcp", python, "trw_mcp.server", validated_target, ui
        )

    # Verify imports (set PYTHONPATH for --pip-target case). Uses the effective
    # interpreter so a PEP 668 venv fallback verifies the venv, not the abandoned
    # system Python.
    env = _verify_package_imports(effective_python, validated_target, ui)

    # Post-install apparatus self-check (2026-04-21, iter-18-replication
    # regression): verify the trw-mcp binary exists AND responds to a MCP
    # initialize+tools/list handshake. A silent install failure that leaves
    # the bin wrapper missing (or the MCP server crashing at startup) is
    # exactly what broke iter-18 replication — every eval run proceeded
    # without any trw_* tools registered, wasting 60 minutes of compute.
    # This check adds ~1s to install time and fails loudly on the exact
    # regression we just lived through.
    if validated_target:
        wrapper = Path(validated_target) / "bin" / "trw-mcp"
        if not wrapper.is_file():
            ui.step_fail(
                f"trw-mcp binary missing at {wrapper} after install "
                "(installer dependency resolution likely failed)"
            )
            sys.exit(1)
        try:
            # Minimal MCP round-trip: init, initialized, tools/list.
            mcp_probe_input = (
                '{"jsonrpc":"2.0","id":1,"method":"initialize",'
                '"params":{"protocolVersion":"2024-11-05","capabilities":{},'
                '"clientInfo":{"name":"installer-preflight","version":"1"}}}\n'
                '{"jsonrpc":"2.0","method":"notifications/initialized"}\n'
                '{"jsonrpc":"2.0","id":2,"method":"tools/list"}\n'
            )
            probe = subprocess.run(
                # stdio is the only transport (HTTP removed 2026-07-10,
                # a0673d9765); pass no --transport so the probe works with
                # both current and older trw-mcp CLIs.
                [str(wrapper), "serve"],
                input=mcp_probe_input,
                capture_output=True,
                text=True,
                timeout=15,
                env=env,
            )
            # Look for a non-trivial tools/list result — trw_session_start
            # is the canonical smoke-test tool name; every trw-mcp version
            # registers it. Absence of this string in stdout means tools did
            # NOT register correctly even if the process exited cleanly.
            if "trw_session_start" not in probe.stdout:
                ui.step_fail(
                    "trw-mcp binary started but failed to register tools "
                    f"(no trw_session_start in stdout, stderr preview: "
                    f"{probe.stderr[:200]!r})"
                )
                sys.exit(1)
        except subprocess.TimeoutExpired:
            ui.step_fail(
                "trw-mcp binary hung during MCP preflight probe "
                "(timed out waiting for tools/list response)"
            )
            sys.exit(1)
        except (OSError, ValueError) as exc:
            ui.step_fail(f"trw-mcp MCP preflight probe failed: {exc}")
            sys.exit(1)
        ui.info("MCP preflight: trw-mcp binary serves tools/list successfully")

    return effective_python


def phase_install_extras(
    ui: UI,
    step: int,
    total: int,
    python: str,
    install_ai: bool,
    install_sqlitevec: bool,
    pip_target: str = "",
    offline: bool = False,
) -> list[str]:
    """Install optional extras as a single step. Returns feature names."""
    ui.step_header(step, total, "Installing extras")
    features: list[str] = []
    if offline:
        # PRD-SEC-006-FR09: only trw-mcp + trw-memory wheels are embedded.
        # Extras resolve from PyPI, which --offline forbids — skip loudly
        # rather than let pip walk its network escalation ladder.
        ui.step_warn("Offline mode: extras require PyPI — skipping AI/vector extras")
        ui.step_warn("Re-run without --offline (or pre-stage a wheelhouse) to add them later")
        return features
    validated_target = validate_pip_target(pip_target)

    if install_ai:
        ui.start_spinner("Installing trw-mcp[ai]...")
        ok = pip_install(python, "trw-mcp[ai]", "trw-mcp[ai]", ui, target_dir=validated_target)
        ui.stop_spinner(ok, "AI features enabled", "AI extras failed (non-fatal)")
        if ok:
            features.append("AI/LLM")
        else:
            ui.step_warn("AI extras failed \u2014 base TRW still works fine")

        # PRD-EVAL-031: when TRW_EMBEDDINGS_AVAILABLE=1, sentence-transformers is
        # already importable via a host-staged read-only overlay mounted at
        # /trw-embeddings (PYTHONPATH = /trw-embeddings/site-packages is set in
        # the container env). Skip the pip install to avoid SSD writes in the
        # SWE-bench eval sandbox where the overlay is present.
        if os.environ.get("TRW_EMBEDDINGS_AVAILABLE") == "1":
            ui.step_ok(
                "Embeddings available via host overlay (PYTHONPATH=/trw-embeddings) \u2014 skipping pip install"
            )
            ui.start_spinner("Verifying embeddings runtime...")
            ok = verify_embeddings_runtime(python, target_dir=validated_target)
            ui.stop_spinner(ok, "Embeddings verified", "Embeddings runtime failed (non-fatal)")
            if ok:
                features.append("embeddings")
            else:
                ui.hint("Run: pip install trw-memory[embeddings]")
        else:
            ui.start_spinner("Installing sentence-transformers (embeddings)...")
            ok = pip_install(
                python,
                "sentence-transformers>=2.0.0",
                "sentence-transformers",
                ui,
                target_dir=validated_target,
            )
            if ok:
                ui.stop_spinner(True, "Embeddings package installed")
                ui.start_spinner("Verifying embeddings runtime...")
                ok = verify_embeddings_runtime(python, target_dir=validated_target)
                ui.stop_spinner(ok, "Embeddings verified", "Embeddings runtime failed (non-fatal)")
            else:
                ui.stop_spinner(False, "Embeddings enabled", "Embeddings install failed (non-fatal)")
            if ok:
                features.append("embeddings")
            else:
                ui.step_warn("embeddings failed \u2014 recall uses keyword-only search")
                ui.hint("Run: pip install trw-memory[embeddings]")

    if install_sqlitevec:
        ui.start_spinner("Installing sqlite-vec...")
        ok = pip_install(python, "sqlite-vec", "sqlite-vec", ui, target_dir=validated_target)
        ui.stop_spinner(ok, "sqlite-vec enabled", "sqlite-vec failed (non-fatal)")
        if ok:
            # Smoke test: verify the installed Python can actually load the
            # sqlite-vec extension. macOS system Python and some python.org
            # builds ship sqlite3 compiled without SQLITE_ENABLE_LOAD_EXTENSION,
            # which makes enable_load_extension() raise AttributeError or
            # OperationalError. Installing the wheel in that environment is a
            # silent trap — the user sees success here, then every trw_learn
            # call fails with "sqlite extension error" later.
            ui.start_spinner("Verifying sqlite-vec loads on this Python...")
            smoke = _run_quiet(
                [
                    python,
                    "-c",
                    "import sqlite3, sqlite_vec; "
                    "c = sqlite3.connect(':memory:'); "
                    "c.enable_load_extension(True); "
                    "sqlite_vec.load(c); "
                    "c.enable_load_extension(False); "
                    "c.close()",
                ],
                timeout=15,
            )
            if smoke:
                ui.stop_spinner(True, "sqlite-vec verified")
                features.append("sqlite-vec")
            else:
                ui.stop_spinner(False, "sqlite-vec cannot load on this Python")
                ui.step_warn(
                    "Your Python was built without SQLITE_ENABLE_LOAD_EXTENSION."
                )
                ui.step_warn(
                    "TRW will run with BM25 keyword search only (vector search disabled)."
                )
                if sys.platform == "darwin":
                    ui.hint("To enable vector search on macOS, install a Python with extension support:")
                    ui.hint("  brew install python@3.12")
                    ui.hint("  OR download from https://www.python.org/downloads/")
                    ui.hint("Then re-run the installer with that Python.")
                else:
                    ui.hint("Rebuild Python with --enable-loadable-sqlite-extensions or install")
                    ui.hint("a distribution that enables it (most pyenv/uv/conda builds do).")
        else:
            ui.step_warn("sqlite-vec failed \u2014 TRW works without it")

    return features


