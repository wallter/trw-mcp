def _detect_installed_clis() -> list[str]:
    """Detect which AI coding CLI binaries are on PATH."""
    detected = []
    if shutil.which("claude"):
        detected.append("claude-code")
    if shutil.which("cursor-agent"):
        detected.append("cursor-cli")
    if shutil.which("cursor"):
        detected.append("cursor-ide")
    if shutil.which("opencode"):
        detected.append("opencode")
    if shutil.which("codex"):
        detected.append("codex")
    if shutil.which("github-copilot") or shutil.which("copilot"):
        detected.append("copilot")
    if shutil.which("agy") or shutil.which("antigravity"):
        detected.append("antigravity-cli")
    return _unique(detected)


def _detect_project_ides(project_dir: str) -> list[str]:
    """Detect which IDE configs exist in the project."""
    detected = []
    if os.path.isdir(os.path.join(project_dir, ".claude")):
        detected.append("claude-code")
    cursor_dir = os.path.join(project_dir, ".cursor")
    if os.path.isfile(os.path.join(cursor_dir, "cli.json")):
        detected.append("cursor-cli")
    if os.path.isdir(cursor_dir):
        detected.append("cursor-ide")
    if os.path.isdir(os.path.join(project_dir, ".opencode")) or os.path.isfile(
        os.path.join(project_dir, "opencode.json")
    ):
        detected.append("opencode")
    if os.path.isdir(os.path.join(project_dir, ".codex")) or os.path.isfile(
        os.path.join(project_dir, ".codex", "config.toml")
    ):
        detected.append("codex")
    github_agents_dir = os.path.join(project_dir, ".github", "agents")
    try:
        has_copilot_agents = os.path.isdir(github_agents_dir) and any(
            name.endswith(".agent.md") for name in os.listdir(github_agents_dir)
        )
    except OSError:
        # Permission-denied .github/agents must degrade to "not detected",
        # never crash client detection.
        has_copilot_agents = False
    if os.path.isfile(os.path.join(project_dir, ".github", "copilot-instructions.md")) or has_copilot_agents:
        detected.append("copilot")
    # gemini/aider detection removed with the profiles' retirement (2026-07-11):
    # a leftover .gemini/ or .aider.conf.yml must not auto-select a retired id.
    return _unique(detected)


# ── User-scope (machine-local) tier provisioning (PRD-SEC-006-FR06) ──
# The PRD-CORE-185 FR09 auto-detect heuristic (_detect_user_scope /
# _user_scope_markers) was removed: provisioning is consent-only now, so
# detection had no production caller.


def _resolve_user_tier_consent(
    ui: UI,
    *,
    interactive: bool,
    opt_user_tier: bool | None,
) -> bool:
    """Resolve whether to provision the ~/.trw user-scope tier (FR06).

    Default is PROJECT-ONLY. ``~/.trw`` is provisioned only with explicit
    consent:
      * ``--user-tier`` / ``--no-user-tier`` (or ``TRW_USER_TIER`` env) wins.
      * Interactive: a yes/no prompt, defaulting to NO.
      * Non-interactive without a flag: NO (project-only).

    PRD-SEC-006-FR06 supersedes the prior auto-detect behavior (PRD-CORE-185
    FR09), which provisioned ``~/.trw`` whenever home markers were present —
    that violated the consent requirement.
    """
    if opt_user_tier is None:
        env_val = os.environ.get("TRW_USER_TIER", "").strip().lower()
        if env_val in {"1", "true", "yes"}:
            opt_user_tier = True
        elif env_val in {"0", "false", "no"}:
            opt_user_tier = False
    if opt_user_tier is True:
        return True
    if opt_user_tier is False:
        return False
    if interactive:
        ui.hint("The user-scope tier (~/.trw) shares memory across all your projects on this machine.")
        return prompt_yes_no("Provision the machine-local ~/.trw user-scope tier?", default="n")
    # Non-interactive default: project-only.
    return False


def _provision_user_scope(consented: bool) -> bool:
    """Non-destructively seed the user-scope store + ``~/.trw/config.yaml`` (FR06).

    PRD-SEC-006-FR06: provisions ONLY when *consented* is True (resolved by
    :func:`_resolve_user_tier_consent`). Ensures the machine-local memory dir
    parent exists and that the machine-defaults config carries
    ``user_tier_enabled``. NEVER clobbers an existing ``~/.trw/config.yaml`` key
    (merge/skip only) and NEVER touches project data. Returns True if a user
    scope was provisioned, False otherwise (no-op). No network.
    """
    if not consented:
        return False

    home = Path.home()
    trw_home = home / ".trw"
    # Machine-local memory store parent (resolve_user_memory_dir's ~/.trw fallback).
    (trw_home / "memory").mkdir(parents=True, exist_ok=True)

    config_path = trw_home / "config.yaml"
    existing_keys: set[str] = set()
    if config_path.is_file():
        existing_keys = set(_parse_simple_yaml(config_path.read_text(encoding="utf-8")).keys())

    # Seed only keys not already present (non-destructive merge/skip).
    seed: dict[str, str] = {"user_tier_enabled": "true"}
    additions = [f"{k}: {v}\n" for k, v in seed.items() if k not in existing_keys]
    if not additions:
        return True

    header = "" if config_path.is_file() else "# TRW machine-defaults (PRD-CORE-185 user-space tier)\n"
    with config_path.open("a", encoding="utf-8") as handle:
        handle.write(header)
        handle.writelines(additions)
    return True


def _prompt_ide_selection(
    detected_clis: list[str],
    detected_ides: list[str],
    prior_targets: list[str] | None = None,
) -> list[str] | None:
    """Prompt user to multi-select which client surfaces TRW should configure."""
    if detected_clis:
        print(f"{GREEN}[TRW]{NC} Detected CLI(s): {_format_ide_list(detected_clis)}")
    if detected_ides:
        print(f"{GREEN}[TRW]{NC} Existing client config: {_format_ide_list(detected_ides)}")

    default_targets = _normalize_ide_targets(prior_targets or _unique(detected_ides + detected_clis))
    if not default_targets:
        default_targets = ["claude-code"]

    tty = _open_tty()
    if tty is None or not sys.stdout.isatty():
        return _prompt_ide_selection_fallback(default_targets)

    cursor = 0
    selected = set(default_targets)
    rendered_lines = 0

    try:
        sys.stdout.write("\033[?25l")
        while True:
            lines = [
                "",
                "Configure TRW for one or more clients:",
                f"{DIM}Use ↑/↓ (or j/k) to move, space to toggle, a = all, n = none, enter = continue, s = skip.{NC}",
            ]
            for idx, ide in enumerate(_SUPPORTED_IDES):
                meta = _IDE_META[ide]
                pointer = f"{BOLD}>{NC}" if idx == cursor else " "
                checked = f"{GREEN}[x]{NC}" if ide in selected else "[ ]"
                lines.append(f"  {pointer} {checked} {meta['label']}")
                lines.append(f"      {DIM}{meta['summary']}{NC}")

            if rendered_lines:
                sys.stdout.write(f"\033[{rendered_lines}F")
            for line in lines:
                sys.stdout.write(f"\r\033[K{line}\n")
            sys.stdout.flush()
            rendered_lines = len(lines)

            key = _read_single_key(tty)
            if key is None:
                return _prompt_ide_selection_fallback(default_targets)
            if key in ("\x03",):
                raise KeyboardInterrupt
            if key in ("UP", "k", "K"):
                cursor = (cursor - 1) % len(_SUPPORTED_IDES)
                continue
            if key in ("DOWN", "j", "J"):
                cursor = (cursor + 1) % len(_SUPPORTED_IDES)
                continue
            if key in ("SPACE", "x", "X"):
                current = _SUPPORTED_IDES[cursor]
                if current in selected:
                    selected.remove(current)
                else:
                    selected.add(current)
                continue
            if key in ("a", "A"):
                selected = set(_SUPPORTED_IDES)
                continue
            if key in ("n", "N"):
                selected.clear()
                continue
            if key in ("s", "S"):
                print()
                return None
            if key == "ENTER":
                chosen = [ide for ide in _SUPPORTED_IDES if ide in selected]
                return chosen or default_targets
    finally:
        print()
        sys.stdout.write("\033[?25h")
        sys.stdout.flush()
        tty.close()


