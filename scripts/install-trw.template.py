# ── Wheel extraction ─────────────────────────────────────────────────


def _extract_wheel_data(marker: str) -> bytes:
    """Read base64 data following *marker* from this script file.

    Wheels are embedded as comment lines after marker comments at the
    end of the file (e.g. MEMORY_WHEEL_DATA, WHEEL_DATA). Each marker
    is followed by comment-prefixed base64 lines.

    NOTE: build_installer.py's ``_validate_py_installer`` re-implements this
    parse deliberately — this file must stay import-free/standalone, and the
    build-time validator proving the embed format round-trips must not share
    code with the thing it validates. Change the embed format in BOTH places.
    """
    script_path = Path(sys.argv[0]).resolve() if sys.argv[0] != "-" else Path(__file__).resolve()
    try:
        text = script_path.read_text(encoding="utf-8", errors="replace")
    except OSError as exc:
        # Piped execution (curl ... | python3 -) leaves no on-disk source to
        # re-read (__file__ is '<stdin>'), so the embedded wheels are
        # unreachable. Fail with instructions instead of a raw traceback.
        raise RuntimeError(
            "cannot re-read the installer source to extract the embedded "
            f"wheels ({exc}). Piped execution (curl ... | python3 -) is not "
            "supported — download to a file first: "
            "curl -fsSL <url> -o install-trw.py && python3 install-trw.py"
        ) from exc

    collecting = False
    b64_chunks: list[str] = []

    for line in text.splitlines():
        stripped = line.strip()
        if stripped == f"# {marker}":
            collecting = True
            continue
        if collecting:
            if stripped.startswith("# __") and stripped.endswith("__"):
                break
            if stripped.startswith("# "):
                b64_chunks.append(stripped[2:])
            elif stripped == "#":
                continue
            else:
                break

    if not b64_chunks:
        raise RuntimeError(f"No embedded data found for marker {marker}")

    return base64.b64decode("".join(b64_chunks))


def _verify_checksum(data: bytes, expected_sha256: str, label: str) -> None:
    """Verify SHA-256 checksum of extracted wheel data.

    Skips verification if expected_sha256 is empty or a template placeholder.
    """
    if not expected_sha256 or expected_sha256.startswith("{{"):
        return
    actual = hashlib.sha256(data).hexdigest()
    if actual != expected_sha256:
        raise RuntimeError(f"Checksum mismatch for {label}: expected {expected_sha256[:16]}..., got {actual[:16]}...")


def extract_wheels(tmpdir: Path) -> tuple[Path, Path]:
    """Extract all embedded wheels into *tmpdir*.

    Returns (memory_wheel_path, mcp_wheel_path).
    Verifies SHA-256 checksums when available (set by build_installer.py).
    """
    memory_data = _extract_wheel_data("__MEMORY_WHEEL_DATA__")
    _verify_checksum(memory_data, MEMORY_WHEEL_SHA256, MEMORY_WHEEL_FILENAME)
    memory_path = tmpdir / MEMORY_WHEEL_FILENAME
    memory_path.write_bytes(memory_data)

    mcp_data = _extract_wheel_data("__WHEEL_DATA__")
    _verify_checksum(mcp_data, WHEEL_SHA256, WHEEL_FILENAME)
    mcp_path = tmpdir / WHEEL_FILENAME
    mcp_path.write_bytes(mcp_data)

    return memory_path, mcp_path


# ── pip install with fallback ────────────────────────────────────────


def _pip_target_from_cmd(cmd: list[str]) -> str:
    """Extract ``--target DIR`` from a pip command when present."""
    for index, part in enumerate(cmd):
        if part == "--target" and index + 1 < len(cmd):
            return cmd[index + 1]
    return ""


def _build_pip_runtime_env(target_dir: str = "") -> dict[str, str]:
    """Build env that keeps installer subprocess writes inside the tmpfs target."""
    env = dict(os.environ)
    env["PYTHONDONTWRITEBYTECODE"] = "1"
    if not target_dir:
        return env

    target_path = Path(target_dir)
    cache_root = target_path / ".cache"
    data_root = target_path / ".local" / "share"
    pip_cache_dir = cache_root / "pip"
    tmp_dir = target_path / ".tmp"
    pip_cache_dir.mkdir(parents=True, exist_ok=True)
    data_root.mkdir(parents=True, exist_ok=True)
    tmp_dir.mkdir(parents=True, exist_ok=True)
    env["PIP_NO_CACHE_DIR"] = "1"
    env["PIP_CACHE_DIR"] = str(pip_cache_dir)
    env["XDG_CACHE_HOME"] = str(cache_root)
    # Runtime imports in the MCP preflight can initialize dependency metadata
    # caches (for example FastMCP's version cache). Keep those XDG data writes
    # on the same tmpfs as the installed packages instead of leaking to HOME.
    env["XDG_DATA_HOME"] = str(data_root)
    env["TMPDIR"] = str(tmp_dir)
    # uv backend parity: keep uv's cache inside the target tmpfs too (uv ignores
    # the PIP_* vars). Harmless when the pip backend is used.
    env["UV_CACHE_DIR"] = str(cache_root / "uv")
    env["UV_NO_CACHE"] = "1"
    return env


_LAST_RUN_QUIET_STDERR = ""


def _run_quiet(cmd: list[str], timeout: int = 120) -> bool:
    """Run a command silently, return True if exit code == 0.

    *timeout* (seconds, default 120) prevents hangs when pip stalls on
    PEP 668 externally-managed system Pythons without a venv.

    ``KeyboardInterrupt`` is intentionally not caught — it propagates to
    the caller so the user can abort the entire installer with Ctrl-C.

    The last ~2000 chars of stderr are stashed in ``_LAST_RUN_QUIET_STDERR``
    so ``pip_install``'s final failure message can show the REAL error
    instead of guessing PEP 668 for every failure mode.
    """
    global _LAST_RUN_QUIET_STDERR
    try:
        env = _build_pip_runtime_env(_pip_target_from_cmd(cmd))
        proc = subprocess.run(
            cmd,
            env=env,
            stdout=subprocess.DEVNULL,
            stderr=subprocess.PIPE,
            text=True,
            timeout=timeout,
        )
        _LAST_RUN_QUIET_STDERR = (getattr(proc, "stderr", "") or "")[-2000:]
        return proc.returncode == 0
    except FileNotFoundError as exc:
        _LAST_RUN_QUIET_STDERR = str(exc)
        return False
    except subprocess.TimeoutExpired:
        _LAST_RUN_QUIET_STDERR = f"command timed out after {timeout}s: {cmd[0]}"
        return False



def _run_python_smoke(cmd: list[str], target_dir: str = "", timeout: int = 120) -> bool:
    """Run a Python smoke command with installer target path on PYTHONPATH."""
    try:
        env = _build_pip_runtime_env(target_dir)
        if target_dir:
            env["PYTHONPATH"] = target_dir + os.pathsep + env.get("PYTHONPATH", "")
        return (
            subprocess.run(  # noqa: S603 -- installer executes its own Python smoke command
                cmd,
                env=env,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                timeout=timeout,
            ).returncode
            == 0
        )
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return False


def verify_embeddings_runtime(python: str, target_dir: str = "") -> bool:
    """Return True when the installed runtime can create the embedding provider."""
    return _run_python_smoke(
        [
            python,
            "-B",
            "-c",
            (
                "from trw_memory.embeddings.local import LocalEmbeddingProvider; "
                "provider = LocalEmbeddingProvider(); "
                "raise SystemExit(0 if provider.available() else 1)"
            ),
        ],
        target_dir=target_dir,
        timeout=180,
    )


def _wheel_runtime_dependencies_satisfied(wheel_path: Path) -> bool:
    """Return True when installed packages already satisfy wheel dependencies."""
    try:
        from packaging.requirements import Requirement

        with zipfile.ZipFile(wheel_path) as wheel:
            metadata_name = next(name for name in wheel.namelist() if name.endswith(".dist-info/METADATA"))
            metadata = BytesParser().parsebytes(wheel.read(metadata_name))
    except (ModuleNotFoundError, OSError, StopIteration, zipfile.BadZipFile, KeyError):
        return False

    for requirement_text in metadata.get_all("Requires-Dist", []):
        try:
            requirement = Requirement(requirement_text)
        except Exception:
            return False
        if requirement.marker and not requirement.marker.evaluate({"extra": ""}):
            continue
        try:
            installed_version = importlib_metadata.version(requirement.name)
        except importlib_metadata.PackageNotFoundError:
            return False
        if requirement.specifier and not requirement.specifier.contains(installed_version, prereleases=True):
            return False
    return True


def pip_install(python: str, package: str, label: str, ui: UI, target_dir: str = "") -> bool:
    """Try pip install with escalating fallbacks.

    When *target_dir* is set, passes ``--target DIR`` to pip so packages
    are written to that directory instead of site-packages.  This is used
    to redirect writes to a tmpfs mount (PRD-INFRA-058).

    Tries: normal -> --user -> [break-system, opt-in] -> dedicated venv. The
    --user / --break-system-packages escalations are pip-only (PEP 668 system
    Pythons); the uv backend installs into --target directly and needs neither.
    On a PEP 668 Python where system mutation is declined, the final rung is a
    dedicated importable venv (_ensure_fallback_venv) — NOT pipx, because
    downstream steps must ``import trw_mcp`` in the same interpreter.
    """
    # If an earlier package already fell back to a dedicated venv (PEP 668),
    # route THIS install into the same venv so a single interpreter owns the
    # whole stack downstream. (--pip-target has its own isolation; never here.)
    if _FALLBACK_VENV_PYTHON and not target_dir:
        if _run_quiet(build_install_cmd(_FALLBACK_VENV_PYTHON, ui, [package])):
            return True
        ui.step_warn(f"Failed to install {label} into fallback venv {_fallback_venv_dir()}")
        return False

    kind = resolve_install_backend(python, ui)[0]
    # 2026-04-21: keep pip's resolver fetching external deps (structlog,
    # pydantic, ...) from PyPI; only force --no-deps for a bundled wheel whose
    # runtime deps are already satisfied. The combined-wheel install in
    # phase_install_packages handles the trw-memory<-trw-mcp internal dep.
    no_deps = bool(
        target_dir and package.endswith(".whl") and _wheel_runtime_dependencies_satisfied(Path(package))
    )
    base = build_install_cmd(python, ui, [package], target_dir=target_dir, no_deps=no_deps)

    if _run_quiet(base):
        return True

    if kind != "pip":
        return False

    if not target_dir and _run_quiet(base + ["--user"]):
        ui.step_warn(f"Installed {label} with --user (PEP 668 managed environment)")
        return True

    # PRD-SEC-006-FR03: --break-system-packages mutates a system Python's
    # site-packages and can corrupt OS-managed packages. It is NEVER applied
    # silently — only when the operator opted in via --allow-system-python (or
    # an interactive confirmation, resolved into _ALLOW_SYSTEM_PYTHON).
    if _allow_system_python(ui) and _run_quiet(base + ["--break-system-packages"]):
        ui.step_warn(f"Installed {label} with --break-system-packages (--allow-system-python)")
        return True

    # Final rung: a dedicated, importable venv. Reached on a PEP 668 system
    # Python when the operator declined --break-system-packages (or it failed).
    # This is the third-stage-bootstrap analogue of the shell bootstraps' pipx
    # rung — but a real venv rather than pipx, because the installer's downstream
    # steps (force-pin, import verification, MCP preflight, client config) must
    # import trw_mcp/trw_memory from the SAME interpreter, which pipx's isolated
    # console-script-only install cannot provide. (--pip-target is its own
    # isolation and is never rerouted to the venv.)
    if not target_dir:
        venv_python = _ensure_fallback_venv(python, ui)
        if venv_python and _run_quiet(build_install_cmd(venv_python, ui, [package])):
            ui.step_warn(
                f"Installed {label} into an isolated venv "
                f"({_fallback_venv_dir()}) — PEP 668 system Python left untouched"
            )
            return True

    stderr_tail = _LAST_RUN_QUIET_STDERR.strip()
    if "externally-managed-environment" in stderr_tail or not stderr_tail:
        ui.step_fail(
            f"Could not install {label}: this Python is externally managed (PEP 668) "
            "and the isolated-venv fallback did not succeed."
        )
    else:
        # The failure was NOT the PEP 668 case — show the actual pip error
        # instead of misdiagnosing (e.g. broken proxy, disk full, bad spec).
        ui.step_fail(f"Could not install {label} — last pip error:")
        for line in stderr_tail.splitlines()[-5:]:
            ui.step_warn(f"  {line.strip()[:160]}")
    ui.step_warn("Use an isolated environment instead, e.g.:")
    ui.step_warn("  python3 -m venv .venv && . .venv/bin/activate   (then re-run)")
    ui.step_warn("  pipx install trw-mcp                            (CLI-only)")
    ui.step_warn("Or pass --allow-system-python to override (not recommended).")

    return False


def validate_pip_target(target_dir: str) -> str:
    """Validate --pip-target to reject shell metacharacters and malformed paths."""
    if not target_dir:
        return ""
    if not re.fullmatch(r"[A-Za-z0-9_./-]+", target_dir):
        raise ValueError(f"Invalid --pip-target: {target_dir!r}")
    return target_dir


# ── install backend resolution (pip vs uv) ───────────────────────────
#
# A uv-managed CPython (python-build-standalone, what `uv python install`
# fetches) frequently ships WITHOUT a usable `pip`/`ensurepip`, so
# `python -m pip install` fails with "No module named pip" and the
# --user / --break-system-packages escalations — which all reuse
# `-m pip` — fail too. We resolve a working install backend ONCE and
# route every install through it:
#   1. standard Python with pip            -> python -m pip
#   2. pip missing but ensurepip present   -> bootstrap, then python -m pip
#   3. uv-managed (no pip, uv on PATH)      -> uv pip install --python <python>
# Dynamic override (handles both situations explicitly):
#   TRW_INSTALL_BACKEND = auto | pip | uv   (default auto)
#   TRW_UV_BIN          = /path/to/uv       (else found on PATH)

_INSTALL_BACKEND: tuple[str, list[str]] | None = None

# PRD-SEC-006-FR03: tri-state consent for --break-system-packages.
#   None  -> not yet resolved; resolve lazily (interactive prompt or default-off)
#   True  -> operator opted in (--allow-system-python / TRW_ALLOW_SYSTEM_PYTHON
#            / interactive yes)
#   False -> explicitly declined
_ALLOW_SYSTEM_PYTHON: bool | None = None


def set_allow_system_python(allowed: bool | None) -> None:
    """Set the resolved --break-system-packages consent (called from main())."""
    global _ALLOW_SYSTEM_PYTHON
    _ALLOW_SYSTEM_PYTHON = allowed


def _allow_system_python(ui: UI) -> bool:
    """Return whether --break-system-packages may be applied (FR03).

    Consent is explicit and never silent. Resolution order:
      1. A previously-resolved value (from the --allow-system-python flag or
         TRW_ALLOW_SYSTEM_PYTHON env, set via set_allow_system_python).
      2. An interactive confirmation prompt (default NO).
      3. Otherwise (non-interactive, no flag): DENY.
    """
    global _ALLOW_SYSTEM_PYTHON
    if _ALLOW_SYSTEM_PYTHON is not None:
        return _ALLOW_SYSTEM_PYTHON
    if ui.interactive:
        ui.step_warn(
            "This Python is externally managed (PEP 668). Installing into it "
            "with --break-system-packages can corrupt OS packages."
        )
        decision = prompt_yes_no(
            "Allow --break-system-packages on this system Python?", default="n"
        )
        _ALLOW_SYSTEM_PYTHON = decision
        return decision
    # Non-interactive without explicit opt-in: deny.
    _ALLOW_SYSTEM_PYTHON = False
    return False


# ── PEP 668 dedicated-venv fallback (importable install) ─────────────
#
# When the target Python is externally managed (PEP 668) and the operator has
# NOT opted into --break-system-packages, both plain and --user pip refuse. The
# shell bootstraps fall back to pipx here, but pipx is INSUFFICIENT for THIS
# installer: pipx exposes only the `trw-mcp` console script from a hidden
# isolated venv, whereas every downstream step needs trw_mcp AND trw_memory to
# be IMPORTABLE in the SAME interpreter — the bundled-wheel force-pin,
# _verify_package_imports, the MCP preflight probe, and the emitted client MCP
# config's `python -m trw_mcp.server` fallback all resolve against `$PYTHON`.
# So we create/reuse a DEDICATED venv (a venv created from a PEP 668 Python is
# itself NOT externally managed, so pip works normally inside it), install into
# it, and rebind the interpreter for all subsequent phases.
#
# Knob: TRW_FALLBACK_VENV overrides the default ~/.trw/venv location.
_FALLBACK_VENV_PYTHON: str | None = None


def _fallback_venv_dir() -> Path:
    """Return the dedicated fallback-venv directory (TRW_FALLBACK_VENV knob)."""
    override = os.environ.get("TRW_FALLBACK_VENV", "").strip()
    if override:
        return Path(override).expanduser()
    return Path.home() / ".trw" / "venv"


def _fallback_venv_interpreter(venv_dir: Path) -> Path:
    """Return the interpreter path inside *venv_dir* (POSIX/Windows layout)."""
    if os.name == "nt":
        return venv_dir / "Scripts" / "python.exe"
    return venv_dir / "bin" / "python"


def _ensure_fallback_venv(source_python: str, ui: UI) -> str | None:
    """Create/reuse a dedicated venv and return its interpreter, or None.

    Idempotent: the resolved interpreter is cached in the module-global
    ``_FALLBACK_VENV_PYTHON`` so a second package install reuses the same venv.
    Creation uses *source_python*'s ``-m venv``. On success the cached install
    backend (``_INSTALL_BACKEND``) is RESET so subsequent ``build_install_cmd``
    calls re-resolve for the venv interpreter instead of the abandoned system
    Python (the backend is cached on first resolution, keyed on the first
    interpreter seen).
    """
    global _FALLBACK_VENV_PYTHON, _INSTALL_BACKEND
    if _FALLBACK_VENV_PYTHON:
        return _FALLBACK_VENV_PYTHON
    venv_dir = _fallback_venv_dir()
    venv_python = _fallback_venv_interpreter(venv_dir)
    if not venv_python.is_file():
        ui.step_warn(f"Creating an isolated venv at {venv_dir} (PEP 668 fallback)...")
        try:
            venv_dir.parent.mkdir(parents=True, exist_ok=True)
            rc = subprocess.run(  # noqa: S603 -- installer creates its own venv
                [source_python, "-m", "venv", str(venv_dir)],
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                timeout=180,
            ).returncode
        except (OSError, subprocess.TimeoutExpired):
            rc = 1
        if rc != 0 or not venv_python.is_file():
            ui.step_warn(f"Could not create fallback venv at {venv_dir}")
            return None
    _FALLBACK_VENV_PYTHON = str(venv_python)
    # Invalidate the cached backend so the venv interpreter is re-resolved.
    _INSTALL_BACKEND = None
    return _FALLBACK_VENV_PYTHON


def _python_has_pip(python: str) -> bool:
    """True when ``python -m pip`` is importable + runnable."""
    return _run_quiet([python, "-m", "pip", "--version"], timeout=30)


def _find_uv() -> str:
    """Locate the uv binary (TRW_UV_BIN override, else PATH)."""
    return (os.environ.get("TRW_UV_BIN", "").strip() or shutil.which("uv") or "").strip()


def resolve_install_backend(python: str, ui: UI) -> tuple[str, list[str]]:
    """Resolve how to install packages for *python*. Returns ``(kind, prefix)``.

    ``kind`` is ``"pip"`` or ``"uv"``; ``prefix`` is the argv up to and
    including ``install``. Resolved once per run and cached. Exits with an
    actionable message when no backend is usable.
    """
    global _INSTALL_BACKEND
    if _INSTALL_BACKEND is not None:
        return _INSTALL_BACKEND

    requested = os.environ.get("TRW_INSTALL_BACKEND", "auto").strip().lower()
    uv_bin = _find_uv()

    def _uv_backend() -> tuple[str, list[str]]:
        if not uv_bin:
            ui.error("Install backend 'uv' requested but the 'uv' binary was not found.")
            ui.error("  Install uv (https://astral.sh/uv) or set TRW_UV_BIN=/path/to/uv.")
            sys.exit(1)
        ui.step_warn(f"Using uv pip backend ({uv_bin}) for a uv-managed Python")
        return ("uv", [uv_bin, "pip", "install", "--python", python])

    def _pip_backend() -> tuple[str, list[str]]:
        return ("pip", [python, "-B", "-m", "pip", "install"])

    if requested == "uv":
        _INSTALL_BACKEND = _uv_backend()
    elif requested == "pip":
        _INSTALL_BACKEND = _pip_backend()
    elif _python_has_pip(python):
        _INSTALL_BACKEND = _pip_backend()
    else:
        # pip absent — typical of uv-managed CPython. Try to bootstrap pip via
        # ensurepip first (cheapest, keeps the standard path); else use uv.
        ui.step_warn("`python -m pip` unavailable — attempting to bootstrap it via ensurepip")
        if _run_quiet([python, "-m", "ensurepip", "--upgrade"], timeout=180) and _python_has_pip(python):
            ui.step_warn("Bootstrapped pip via ensurepip")
            _INSTALL_BACKEND = _pip_backend()
        elif uv_bin:
            _INSTALL_BACKEND = _uv_backend()
        else:
            ui.error("This Python has no pip, ensurepip could not bootstrap it, and 'uv' is not installed.")
            ui.error("This is common for a uv-managed CPython. Fix one of:")
            ui.error("  - install uv:   curl -LsSf https://astral.sh/uv/install.sh | sh   (auto-detected on re-run)")
            ui.error("  - force a backend:   TRW_INSTALL_BACKEND=uv   (with uv on PATH or TRW_UV_BIN set)")
            ui.error("  - run under a Python that has pip:   python3 -m venv .venv && . .venv/bin/activate")
            sys.exit(1)
    return _INSTALL_BACKEND


def build_install_cmd(
    python: str,
    ui: UI,
    packages: list[str],
    *,
    target_dir: str = "",
    find_links: str = "",
    no_deps: bool = False,
    no_index: bool = False,
    no_cache: bool = False,
    upgrade: bool = True,
    quiet: bool = True,
    no_warn_script_location: bool = False,
) -> list[str]:
    """Build a backend-correct ``install`` command for *packages*.

    Emits pip or uv-pip flags as appropriate (e.g. ``--no-cache-dir`` for pip
    vs ``--no-cache`` for uv; pip-only flags are dropped under uv). ``--target``
    is honored by both backends. Packages/wheel paths come last.
    """
    kind, prefix = resolve_install_backend(python, ui)
    cmd = list(prefix)
    if upgrade:
        cmd.append("--upgrade")
    if quiet:
        cmd.append("--quiet")
    if no_deps:
        cmd.append("--no-deps")
    if no_index:
        cmd.append("--no-index")
    if no_cache:
        cmd.append("--no-cache-dir" if kind == "pip" else "--no-cache")
    if no_warn_script_location and kind == "pip":
        cmd.append("--no-warn-script-location")
    if find_links:
        cmd += ["--find-links", find_links]
    if target_dir:
        cmd += ["--target", target_dir]
    cmd += list(packages)
    return cmd


# ── Run command with live progress ───────────────────────────────────


def run_with_progress(ui: UI, fallback_msg: str, cmd: list[str], timeout: int = 180) -> bool:
    """Run *cmd* showing live progress. Returns True on success.

    A watchdog timer kills the subprocess if it hasn't exited after *timeout*
    seconds (default 180).  This prevents the installer from hanging
    indefinitely when the child stalls (e.g. during CLAUDE.md sync).
    """
    try:
        proc = subprocess.Popen(
            cmd,
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
        )
    except FileNotFoundError:
        return False

    assert proc.stdout is not None

    killed_by_watchdog = False

    def _watchdog_kill() -> None:
        nonlocal killed_by_watchdog
        killed_by_watchdog = True
        proc.kill()

    watchdog = threading.Timer(timeout, _watchdog_kill)
    watchdog.daemon = True
    watchdog.start()

    try:
        if ui.interactive:
            ui.start_spinner(fallback_msg)
            file_count = 0
            for line in proc.stdout:
                line = line.strip()
                # Phase markers update the status message (e.g., "Phase: Syncing CLAUDE.md...")
                if line.startswith("Phase:"):
                    phase_label = line.partition(":")[2].strip()
                    ui.update_spinner(f"{fallback_msg} ({file_count} files) {phase_label}")
                elif re.match(r"  *(Updated|Created|Preserved|Skipped|synced|WARNING|Error):? ", line):
                    file_count += 1
                    short = re.sub(r"^  *(Updated|Created|Created \(new\)|Preserved|Skipped|Error): ", "", line)[:60]
                    ui.update_spinner(f"{fallback_msg} ({file_count} files) {DIM}{short}{NC}")
        else:
            for line in proc.stdout:
                if not ui.quiet:
                    print(f"{GREEN}[TRW]{NC}   {line.rstrip()}")

        proc.wait(timeout=10)
    except subprocess.TimeoutExpired:
        proc.kill()
        proc.wait()
    except BaseException:
        # Ctrl-C (KeyboardInterrupt) or SystemExit mid-stream: never orphan
        # the child — subprocess.run() reaps on interrupt, Popen does not.
        proc.kill()
        proc.wait()
        ui.stop_spinner(False, "", f"{fallback_msg} interrupted")
        raise
    finally:
        watchdog.cancel()

    if killed_by_watchdog:
        ui.step_warn(f"{fallback_msg} timed out after {timeout}s")

    return proc.returncode == 0


# ── Config file update ───────────────────────────────────────────────


def write_platform_credentials(config_path: Path, api_key: str) -> Path | None:
    """Write *api_key* to ``.trw/credentials.yaml`` (mode 0600).

    PRD-SEC-005-FR01: the platform bearer credential is stored in an ignored,
    owner-only file sibling to ``config.yaml`` — never in the git-tracked
    config. The chmod is best-effort (Windows has no POSIX mode bits).

    Returns the credentials path on a successful write, or ``None`` when there
    is no key to write.
    """
    if not api_key:
        return None
    credentials_path = config_path.parent / "credentials.yaml"
    credentials_path.parent.mkdir(parents=True, exist_ok=True)
    credentials_path.write_text(
        "# TRW platform credential — ignored by git, mode 0600 (PRD-SEC-005).\n"
        f'platform_api_key: "{api_key}"\n',
        encoding="utf-8",
    )
    try:
        os.chmod(credentials_path, 0o600)
    except OSError:
        # Windows does not honor POSIX mode bits; proceed (NFR03).
        pass
    return credentials_path


def update_config(
    config_path: Path,
    project_name: str,
    api_key: str,
    telemetry_enabled: bool,
    *,
    embeddings_enabled: bool | None = None,
    sqlite_vec_enabled: bool | None = None,
    target_platforms: list[str] | None = None,
    rewrite_platform_urls: bool = True,
) -> bool:
    """Update .trw/config.yaml with installation settings.

    Optional *embeddings_enabled* and *sqlite_vec_enabled* persist feature
    flags so reinstalls skip the "Optional Features" prompts.
    """
    if not config_path.is_file():
        return False

    lines = config_path.read_text(encoding="utf-8").splitlines(keepends=True)
    platform_url = "https://api.trwframework.com"
    effective_target_platforms = target_platforms or None

    updated: set[str] = set()
    out: list[str] = []
    replacing_platform_urls = False
    replacing_target_platforms = False

    for line in lines:
        normalized_line = line if line.endswith("\n") else line + "\n"
        s = normalized_line.lstrip()
        stripped = s.strip()

        if replacing_target_platforms:
            if not stripped or stripped.startswith(("#", "- ")):
                continue
            replacing_target_platforms = False

        if replacing_platform_urls:
            if not stripped or stripped.startswith(("#", "- ")):
                continue
            replacing_platform_urls = False

        if s.startswith("installation_id:"):
            out.append(f"installation_id: {project_name}\n")
            updated.add("installation_id")
            continue
        if s.startswith("platform_api_key:"):
            # PRD-SEC-005-FR01: the credential no longer lives in the
            # git-tracked config.yaml. Blank any existing field (a legacy
            # tracked key is migrated to credentials.yaml by the caller) and
            # never write the secret here for fresh installs.
            out.append('platform_api_key: ""\n')
            updated.add("platform_api_key")
            continue
        if s.startswith("platform_telemetry_enabled:"):
            val = "true" if telemetry_enabled else "false"
            out.append(f"platform_telemetry_enabled: {val}\n")
            updated.add("platform_telemetry_enabled")
            continue
        if s.startswith("embeddings_enabled:") and embeddings_enabled is not None:
            out.append(f"embeddings_enabled: {'true' if embeddings_enabled else 'false'}\n")
            updated.add("embeddings_enabled")
            continue
        if s.startswith("sqlite_vec_enabled:") and sqlite_vec_enabled is not None:
            out.append(f"sqlite_vec_enabled: {'true' if sqlite_vec_enabled else 'false'}\n")
            updated.add("sqlite_vec_enabled")
            continue
        if s.startswith("target_platforms:") and effective_target_platforms is not None:
            out.append("target_platforms:\n")
            out.extend(f'  - "{ide}"\n' for ide in effective_target_platforms)
            updated.add("target_platforms")
            replacing_target_platforms = True
            continue
        if s.startswith("platform_urls:"):
            updated.add("platform_urls")
            if rewrite_platform_urls and (api_key or telemetry_enabled):
                out.append("platform_urls:\n")
                out.append(f'  - "{platform_url}"\n')
                updated.add("platform_urls_written")
                replacing_platform_urls = True
                continue
            out.append(normalized_line)
            continue

        # Skip commented-out platform config hints
        skip_prefixes = (
            "# platform_urls:",
            '#   - "https://api.',
            "# platform_api_key:",
            "# platform_telemetry_enabled:",
            "# Platform telemetry",
        )
        if any(s.startswith(p) for p in skip_prefixes):
            continue

        out.append(normalized_line)

    # Append missing keys
    if "installation_id" not in updated:
        out.append(f"installation_id: {project_name}\n")
    # PRD-SEC-005-FR01: never append the secret into the tracked config.yaml.
    # The key is persisted to .trw/credentials.yaml (0600) by the caller.
    if telemetry_enabled and "platform_telemetry_enabled" not in updated:
        out.append("platform_telemetry_enabled: true\n")
    if rewrite_platform_urls and (api_key or telemetry_enabled) and "platform_urls_written" not in updated:
        out.append("platform_urls:\n")
        out.append(f'  - "{platform_url}"\n')
    if embeddings_enabled and "embeddings_enabled" not in updated:
        out.append("embeddings_enabled: true\n")
    if sqlite_vec_enabled and "sqlite_vec_enabled" not in updated:
        out.append("sqlite_vec_enabled: true\n")
    if effective_target_platforms and "target_platforms" not in updated:
        out.append("target_platforms:\n")
        out.extend(f'  - "{ide}"\n' for ide in effective_target_platforms)

    config_path.write_text("".join(out), encoding="utf-8")
    return True


# ── Health check ─────────────────────────────────────────────────


def _check_backend_health(url: str, timeout: float = 5.0) -> dict[str, object]:
    """Probe a backend health endpoint. Returns status dict.

    Uses only stdlib — no external dependencies.
    """
    import urllib.error
    import urllib.request

    health_url = f"{url.rstrip('/')}/v1/health"
    try:
        req = urllib.request.Request(health_url, method="GET")
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            data = json.loads(resp.read().decode("utf-8"))
            return {"url": url, "reachable": True, "status": data.get("status", "ok")}
    except urllib.error.HTTPError as exc:
        return {"url": url, "reachable": True, "status": f"http-{exc.code}"}
    except Exception:
        return {"url": url, "reachable": False, "status": "unreachable"}


def _check_all_backends(target_dir: Path) -> list[dict[str, object]]:
    """Check connectivity to all configured platform backends.

    Reads platform_urls from config.yaml (simple line parsing) and probes
    each one. Also checks localhost:8000 if a docker-compose.yml exists
    in the project (indicating a local backend may be running).
    """
    results: list[dict[str, object]] = []
    urls: list[str] = []

    # Read platform_urls from config.yaml
    config_path = target_dir / ".trw" / "config.yaml"
    if config_path.is_file():
        try:
            in_urls = False
            for line in config_path.read_text(encoding="utf-8").splitlines():
                stripped = line.strip()
                if stripped.startswith("platform_urls:"):
                    in_urls = True
                    continue
                if in_urls:
                    if stripped.startswith("- "):
                        url = stripped[2:].strip().strip('"').strip("'")
                        if url:
                            urls.append(url)
                    elif stripped and not stripped.startswith("#"):
                        in_urls = False
        except OSError:
            pass

    # Check for local Docker backend
    if (target_dir / "docker-compose.yml").is_file() or (target_dir / "docker-compose.yaml").is_file():
        local_url = "http://localhost:8000"
        if local_url not in urls:
            urls.insert(0, local_url)

    # Probe all backends in parallel for faster results
    if not urls:
        return results
    if len(urls) == 1:
        return [_check_backend_health(urls[0])]

    from concurrent.futures import ThreadPoolExecutor, as_completed
    from concurrent.futures import TimeoutError as _FuturesTimeout

    with ThreadPoolExecutor(max_workers=min(len(urls), 4)) as pool:
        futures = {pool.submit(_check_backend_health, url): url for url in urls}
        try:
            for future in as_completed(futures, timeout=10):
                try:
                    results.append(future.result())
                except Exception:
                    results.append({"url": futures[future], "reachable": False, "status": "error"})
        except _FuturesTimeout:
            # More queued probes than the 10s pool ceiling allows (each probe
            # is individually bounded at 5s, but only 4 run at once). Report
            # the stragglers as timeouts instead of crashing what is by now a
            # fully-successful install.
            for future, url in futures.items():
                if not future.done():
                    future.cancel()
                    results.append({"url": url, "reachable": False, "status": "timeout"})

    # Preserve original URL ordering
    url_order = {url: i for i, url in enumerate(urls)}
    results.sort(key=lambda r: url_order.get(str(r["url"]), 999))
    return results


# ── MCP server restart ───────────────────────────────────────────


def _is_process_alive(pid: int) -> bool:
    """Check if a process with the given PID is running. Cross-platform.

    - Unix/macOS: ``os.kill(pid, 0)`` (signal 0 = existence check).
    - Windows: ``os.kill(pid, 0)`` raises SystemError on Windows
      (CPython issue #14480). Use ``kernel32.OpenProcess`` instead.
    """
    import os

    if sys.platform == "win32":
        try:
            import ctypes

            SYNCHRONIZE = 0x00100000
            handle = ctypes.windll.kernel32.OpenProcess(SYNCHRONIZE, 0, pid)  # type: ignore[union-attr]
            if handle == 0:
                return False
            ctypes.windll.kernel32.CloseHandle(handle)  # type: ignore[union-attr]
            return True
        except (OSError, AttributeError):
            return False
    else:
        try:
            os.kill(pid, 0)
            return True
        except (ProcessLookupError, OSError):
            return False


def _terminate_process(pid: int) -> bool:
    """Terminate a process by PID. Cross-platform.

    - Unix/macOS: sends SIGTERM for graceful shutdown.
    - Windows: ``os.kill(pid, SIGTERM)`` calls ``TerminateProcess``
      (forceful but reliable — no graceful path without
      ``CREATE_NEW_PROCESS_GROUP`` at spawn time). Falls back to
      ``taskkill /PID`` if that fails.

    Returns True if the process was signalled successfully.
    """
    import os
    import signal

    try:
        if sys.platform == "win32":
            # On Windows, os.kill(pid, SIGTERM) calls TerminateProcess —
            # always forceful. No graceful alternative without spawn-time
            # CREATE_NEW_PROCESS_GROUP flag (see uvicorn PR #1909).
            try:
                os.kill(pid, signal.SIGTERM)
                return True
            except OSError:
                # Fallback: taskkill handles more edge cases on Windows
                return _run_quiet(["taskkill", "/PID", str(pid), "/F"])
        else:
            os.kill(pid, signal.SIGTERM)
            return True
    except (ProcessLookupError, PermissionError, OSError):
        return False


def _pid_command_matches_trw(pid: int) -> bool:
    """Best-effort check that *pid*'s command line looks like a trw-mcp server.

    Guards the legacy shared-server cleanup against PID reuse: if the OS
    recycled the PID for an unrelated process, we must not SIGTERM it. On
    POSIX, ``ps -p PID -o args=`` is portable (Linux + macOS). When the
    command line cannot be read (Windows, ``ps`` missing), err on the side
    of NOT killing — the stale PID file is removed either way.
    """
    try:
        out = subprocess.run(
            ["ps", "-p", str(pid), "-o", "args="],
            stdout=subprocess.PIPE,
            stderr=subprocess.DEVNULL,
            text=True,
            timeout=5,
        ).stdout.lower()
    except (OSError, subprocess.TimeoutExpired):
        return False
    return "trw-mcp" in out or "trw_mcp" in out


def _restart_mcp_servers(target_dir: Path, ui: UI) -> None:
    """Restart MCP server(s) after install/upgrade.

    Cross-platform approach:
      - Kill any leftover background HTTP server from removed shared-server
        installs via its PID file (one-time cleanup; the shared HTTP server
        no longer exists — trw-mcp is stdio-only).
      - Write a version sentinel so the server detects the upgrade on the
        next tool call and advises ``/mcp``.
    """

    trw_dir = target_dir / ".trw"
    pid_path = trw_dir / "mcp-server.pid"

    # Legacy shared-server cleanup: kill any leftover background process
    if pid_path.is_file():
        try:
            pid = int(pid_path.read_text(encoding="utf-8").strip())
            if _is_process_alive(pid) and _pid_command_matches_trw(pid):
                if _terminate_process(pid):
                    ui.step_ok("MCP server stopped (will auto-start on next use)")
            # Dead, unidentifiable, or PID reused by another process: never
            # signal — just drop the stale file.
            pid_path.unlink(missing_ok=True)
        except (ValueError, OSError):
            pid_path.unlink(missing_ok=True)

    # Write version sentinel for stdio instances to detect upgrade
    sentinel_path = trw_dir / "installed-version.json"
    try:
        sentinel_path.write_text(
            json.dumps({"version": TRW_VERSION, "timestamp": _iso_now()}),
            encoding="utf-8",
        )
    except OSError:
        pass  # Best-effort

    _write_version_yaml_metadata(target_dir)


def _write_version_yaml_metadata(target_dir: Path) -> None:
    """Best-effort VERSION.yaml refresh for upgrade-only installer runs.

    `init-project` / `update-project` already write this file through the
    installed package. The standalone installer must also refresh it when
    `--upgrade` skips project setup, otherwise `.trw/installed-version.json`
    and `.trw/frameworks/VERSION.yaml` drift apart.
    """
    version_path = target_dir / ".trw" / "frameworks" / "VERSION.yaml"
    try:
        version_path.parent.mkdir(parents=True, exist_ok=True)
        # Fallbacks derive from the DEPLOYED framework bodies when possible —
        # a hardcoded fallback inevitably goes stale (it shipped "v25_TRW" /
        # "v2.0.0" long after v26/v3.x were current).
        framework_version = _deployed_doc_version(
            target_dir / ".trw" / "frameworks" / "FRAMEWORK.md",
            r"(v\d+(?:\.\d+)*_TRW)",
            None,
        )
        aaref_version = _deployed_doc_version(
            target_dir / ".trw" / "frameworks" / "AARE-F-FRAMEWORK.md",
            r"\*\*Version\*\*:\s*(\d+\.\d+\.\d+)",
            None,
            prefix="v",
        )
        if framework_version is None or aaref_version is None:
            return
        version_path.write_text(
            "\n".join(
                [
                    f"framework_version: {framework_version}",
                    f"aaref_version: {aaref_version}",
                    f"trw_mcp_version: {TRW_VERSION}",
                    f"deployed_at: '{_iso_now()}'",
                    "",
                ]
            ),
            encoding="utf-8",
        )
    except OSError:
        pass  # Best-effort; the sentinel remains the runtime restart signal.


def _deployed_doc_version(doc_path: Path, pattern: str, fallback: str | None, prefix: str = "") -> str | None:
    """Extract a version label from a deployed framework doc header (best-effort)."""
    try:
        if doc_path.is_file():
            head = doc_path.read_text(encoding="utf-8", errors="replace")[:2000]
            m = re.search(pattern, head)
            if m:
                return f"{prefix}{m.group(1)}"
    except OSError:
        pass
    return fallback


def _iso_now() -> str:
    """Return current UTC timestamp in ISO 8601 format."""
    from datetime import datetime, timezone

    return datetime.now(timezone.utc).isoformat()


def _resolve_framework_version() -> str:
    """Resolve the installed trw-mcp version, falling back to the build stamp.

    Prefers the freshly-installed package metadata; ``TRW_VERSION`` is the
    build-time placeholder substituted into the generated installer.
    """
    try:
        return importlib_metadata.version("trw-mcp")
    except Exception:  # justified: metadata lookup is best-effort, never block emit
        return TRW_VERSION


# ── PRD-INFRA-150: downgrade guard ───────────────────────────────────
# Default decision labels emitted on the install log surface (NFR03). Kept as
# module constants (no magic strings) so the wording is DRY across the guard,
# the log formatter, and the tests asserting them.
_GUARD_DECISION_KEPT = "kept-installed (newer)"
_GUARD_DECISION_UPGRADE = "installed-bundled (upgrade)"
_GUARD_DECISION_EQUAL = "installed-bundled (equal)"
_GUARD_DECISION_FRESH = "installed-bundled (fresh)"


_PEP440_LITE = re.compile(
    r"^v?(?P<release>\d+(?:\.\d+)*)"
    r"(?:[._-]?(?P<pre_l>a|alpha|b|beta|rc|c)(?P<pre_n>\d*))?"
    r"(?:[._-]?post(?P<post>\d*))?"
    r"(?:[._-]?dev(?P<dev>\d*))?$"
)
_PRE_ORDER = {"a": 0, "alpha": 0, "b": 1, "beta": 1, "rc": 2, "c": 2}


def _version_key_fallback(version: str) -> tuple[object, ...]:
    """PEP-440-lite sort key used when ``packaging`` is unavailable.

    Covers the shapes trw releases actually use — ``X.Y.Z`` plus optional
    ``aN``/``bN``/``rcN``, ``.postN``, ``.devN`` — with PEP 440 ordering
    (dev < pre < release < post) and trailing-zero equality (``1.0 ==
    1.0.0``). Raises ``ValueError`` on anything else so callers keep their
    existing fail-open policy.
    """
    m = _PEP440_LITE.match(version.strip().lower())
    if m is None:
        raise ValueError(f"unparsable version: {version!r}")
    release = [int(x) for x in m.group("release").split(".")]
    while len(release) > 1 and release[-1] == 0:
        release.pop()
    pre_l, pre_n = m.group("pre_l"), m.group("pre_n")
    post, dev = m.group("post"), m.group("dev")
    if pre_l:
        pre = (0, _PRE_ORDER[pre_l], int(pre_n or 0))
    elif dev is not None and post is None:
        # packaging's NegativeInfinity trick: 1.0.dev1 < 1.0a1 < 1.0
        pre = (-1, 0, 0)
    else:
        pre = (1, 0, 0)
    post_key = (1, int(post or 0)) if post is not None else (0, 0)
    dev_key = (0, int(dev or 0)) if dev is not None else (1, 0)
    return (tuple(release), pre, post_key, dev_key)


def _compare_versions(a: str, b: str) -> int:
    """Compare two versions with PEP 440 semantics (PRD-INFRA-150 FR04).

    Returns 1 if ``a > b``, -1 if ``a < b``, 0 if equal. Uses
    ``packaging.version.Version`` so ``0.55.10 > 0.55.9`` (NOT lexicographic).
    Raises ``ValueError`` (``InvalidVersion`` is a subclass) when either
    operand is unparsable — callers decide the fail-open policy.

    ``packaging`` is a third-party import that a bare system python3 often
    lacks. Losing it must NOT silently disable the downgrade guard (the
    callers' broad ``except`` would mislabel every decision as "fresh"), so
    a stdlib PEP-440-lite key takes over when the import fails.
    """
    try:
        from packaging.version import Version
    except ModuleNotFoundError:
        ka, kb = _version_key_fallback(a), _version_key_fallback(b)
        return (ka > kb) - (ka < kb)

    va, vb = Version(a), Version(b)
    if va > vb:
        return 1
    if va < vb:
        return -1
    return 0


def downgrade_guard_decision(installed: str | None, bundled: str) -> tuple[bool, str]:
    """Return ``(skip_force_install, decision_label)`` for one package (FR04).

    The label is one of the ``_GUARD_DECISION_*`` constants and feeds the single
    structured decision log line emitted per package (NFR03).
    """
    if installed is None:
        return False, _GUARD_DECISION_FRESH
    try:
        cmp = _compare_versions(installed, bundled)
    except Exception:  # justified: unparsable installed metadata -> treat as fresh
        return False, _GUARD_DECISION_FRESH
    if cmp > 0:
        return True, _GUARD_DECISION_KEPT
    if cmp == 0:
        return False, _GUARD_DECISION_EQUAL
    return False, _GUARD_DECISION_UPGRADE


def format_guard_log_line(package: str, installed: str | None, bundled: str, decision: str) -> str:
    """Format the single structured decision log line for *package* (NFR03)."""
    shown = installed if installed is not None else "(absent)"
